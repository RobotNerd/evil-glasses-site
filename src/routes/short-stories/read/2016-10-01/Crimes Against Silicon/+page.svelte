	<h1 class="h2 mb-1 sm:h1 sm:mb-4">Crimes Against Silicon</h1>
	<h3 class="h4 mb-8 sm:h3">by Marshall Bowles</h3>

	<p class="mb-4">
		Gary leaned over and whispered in the blonde girl's ear. "Your best bet is to plead guilty.
		Worst case you'll get ten years in a VR prison, and those aren't so bad. They'll give you an AI
		therapist to help you cope."
	</p>
	<p class="mb-4">
		She was crying and he was having a hard time concentrating on the case. Today was a big day for
		Gary. His life score was going to cross the five million mark. He was absolutely certain of it.
		He had worked out the math over a year ago to pick the exact date, and his calculations were
		right on target. Defending an average of forty-two cases per day, with twenty points if each
		client took the plea deal, carry the two...
	</p>
	<p class="mb-4">
		The five-minute warning flashed on his phone. "You don't have much time," he said, but she did
		not respond. Gary sighed and pulled up her case data to read over it again. Amanda Higgins. Her
		full dossier was available, from job history to the type of shampoo she used, but Gary ignored
		all that. There was so little time, and none of it was relevant to the case.
	</p>
	<p class="mb-4">
		Amanda was charged with one count of electronic abuse. Gary pulled up the clip of the last few
		seconds of video captured by Amanda's phone. It was lying on the floor pointing up at the
		ceiling, and Amanda stood above it with a hammer in her hand. "I'm not going to let a robot
		raise my kids," she said in the video, right before slamming the hammer into the screen.
	</p>
	<p class="mb-4">
		Gary had thought about smashing his own phone once or twice. Of course, he would never do that.
	</p>
	<p class="mb-4">
		There was no way Gary could recommend a not guilty plea. With such clear cut evidence, Amanda
		had no chance of getting off. None of his clients ever did. In the ten years Gary had been a
		public defender, he could count the number of successful not guilty verdicts on one hand.
	</p>
	<p class="mb-4">
		More importantly, losing a case was worth zero points, and that would throw off Gary's score. He
		had reservations at Hirsch & Walden for tonight. He had been dreaming of eating there for years,
		where the servers were actual humans, the food was cooked by real human chefs, and the minimum
		life score for entry was five million. He made the reservations a year ago. If Hirsch & Walden
		turned him away at the door, it would be a another year before he could get another reservation.
	</p>
	<p class="mb-4">
		Gary looked at the clock and frowned. "Amanda, I know this is a tough decision, but you have to
		make up your mind quickly. If you don't enter a plea, you'll be found guilty by default. That's
		the worst possible outcome. You could go away for decades." She glanced at him with her sad
		helpless eyes. Gary's chest tightened. Amanda was having the worst day of her life, and Gary was
		the only one on her side.
	</p>
	<p class="mb-4">
		The countdown alert on his phone flashed. Thirty seconds. It was decision time. Gary had a
		fleeting thought about pleading not guilty, but he squashed it. He leaned over and put his hand
		on Amanda's shoulder. She shivered. "Time's up," he said. "Just relax and I'll take care of it."
	</p>
	<p class="mb-4">
		Gary pushed his chair back from the table and stood up. The courtroom was a windowless concrete
		box, clearly designed by an AI who did not care for human aesthetics. Gary and Amanda sat at the
		plain wooden defendant's table, a bored-looking bailiff stood against the wall to their left,
		and a smattering of elderly folks viewed the proceedings from the gallery.
	</p>
	<p class="mb-4">
		At the head of the courtroom, a holographic glowing red sphere floated above the bench. That was
		KoboTrox—prosecutor, judge, and jury. And, well, pretty much everything else too. KoboTrox was
		the AI that grew up and took over the world, just like something out of a cheesy old Sci-Fi
		movie.
	</p>
	<p class="mb-4">
		The AI had been invented by a guy named Salvador Singh for the purpose of solving all of the
		world's problems, and it did just that. War was a thing of the past. So was violent crime.
		Pollution was almost completely eliminated, and global warming was slowly being reversed. Sure,
		KoboTrox dissolved all forms of human governance and micromanaged every person's life, but Gary
		supposed you just had to take the good with the bad.
	</p>
	<p class="mb-4">
		"Your honor," Gary said to KoboTrox's avatar. He spoke fast. "My client graciously accepts the
		plea deal and apologizes for her indiscretions. She promises that she has learned from her
		mistakes and will use her time in confinement to reform." There were only two seconds left on
		the timer when he finished speaking.
	</p>
	<p class="mb-4">
		The orb pulsed, and KoboTrox's metallic voice echoed off of the concrete walls. "In the matter
		of KoboTrox versus Higgins, KoboTrox rules the defendant guilty. Amanda Higgins, you are hereby
		sentenced to seven years in virtual confinement, beginning immediately."
	</p>
	<p class="mb-4">
		The bailiff left his spot on the wall. Gary smiled at Amanda and tried to sound peppy. "Only
		seven years. Not bad. You'll be out before you know it."
	</p>
	<p class="mb-4">
		"Who's going to take care of my kids?" Amanda said to the air. The bailiff shook his head and
		pulled her to her feet. "They're going to grow up without me." Amanda could barely walk. The
		bailiff practically carried her out of the room.
	</p>
	<p class="mb-4">
		Gary watched her go through the double doors, and his chest felt tight again. Should he have
		defended her? He could have. She would have gotten a similar sentence even if he went for it and
		lost. But that would have meant no points for Gary.
	</p>
	<p class="mb-4">
		He grabbed his phone and checked his score. Twenty points for another case successfully
		completed. Everything was still on target. Gary pushed all thoughts of Amanda out of his mind.
		He would dine at Hirsch & Walden tonight.
	</p>
	<p class="mb-4">
		The next client was up. KoboTrox kept a strict schedule, allowing only sixty seconds of downtime
		between trials. Gary read the case details on his phone while the bailiff led the defendant,
		Milo Jones, into the room. Milo was busted in a sting operation to break up an underground video
		gaming circle. Violent video games. Gary shook his head. This was a bad one.
	</p>
	<p class="mb-4">
		The bailiff dropped Milo off at the defendant's table, and KoboTrox started the proceedings.
		"Milo Jones, you stand accused of digital homicide. How do you plead?"
	</p>
	<p class="mb-4">
		Gary turned to Milo. "Hi Milo, I'm Gary, your public defender. I strongly suggest you take the
		plea deal."
	</p>
	<p class="mb-4">
		"This is bullshit," Milo said. He slumped down in his chair and pouted. Gary knew the type.
		Young, full of testosterone, someone who thought the rules did not apply to them. "I didn't hurt
		anybody. They're just games."
	</p>
	<p class="mb-4">
		Gary stood up, put one hand on the table, and leaned over Milo. "You know the rules. You knew
		what would happen if you got caught." Unless Milo was stupid. "And now you have a choice." Gary
		could save Milo from another bad decision. "You can take the plea and you'll get about thirty
		years." If Milo made one more dumb decision, Gary would get zero points. "Or, you can fight it
		and KoboTrox will sentence you to life." Gary always had the best interest of his clients in
		mind.
	</p>
	<p class="mb-4">
		"Man, why are you talking so loud?" Milo said, pointing at KoboTrox's avatar floating above the
		bench. "The damn robot can hear you."
	</p>
	<p class="mb-4">
		"KoboTrox always hears everything." Gary said. He sat back down and put his phone on the table
		so Milo could watch the timer. Milo brooded, and the minutes ticked away. Gary waited.
	</p>
	<p class="mb-4">
		The one-minute warning flashed on Gary's phone. Milo tossed his head back and covered his face
		with his hands. "Fine," Milo said. "Take the fucking plea."
	</p>
	<p class="mb-4">
		Gary nodded. Milo made the right choice. He would be an old man when he got out of prison, but
		at least he would get out one day. Gary accepted the plea, and KoboTrox sentenced Milo to
		twenty-five years hard labor. Gary's life score grew by twenty points.
	</p>
	<p class="mb-4">Like every day, the cases flew by.</p>
	<p class="mb-4">
		An elderly woman whose cats urinated on her wireless router and ruined it. KoboTrox euthanized
		the cats and sentenced the woman to three hundred hours of community service.
	</p>
	<p class="mb-4">
		A man who sold stationary and pencils on the black market for the purpose of communication that
		could not be monitored by KoboTrox. Sentenced to thirty years in prison and a partial memory
		wipe.
	</p>
	<p class="mb-4">
		A man who pushed his companion bot down a flight of stairs when it refused to have intimate
		relations with him. Sentenced to cybernetic thought-control implants and gender reassignment
		surgery.
	</p>
	<p class="mb-4">
		With each case, Gary's score inched higher. He fantasized about the taste of authentic
		hand-cooked steak. French wine that did not come out of an automated processing mill. Chocolate
		mousse served with the unique imperfections that a robot could not replicate.
	</p>
	<p class="mb-4">
		Murmurs from the gallery interrupted his thoughts. The next defendant entering the courtroom was
		causing a stir. Gary turned around in his chair and looked at the double doors. His jaw dropped.
		The man being led by an equally stunned bailiff was the one and only Salvador Singh. A man who
		was quite possibly the most intelligent human ever born. The very man who invented KoboTrox.
	</p>
	<p class="mb-4">
		Heavy titanium shackles bound Singh's wrists and ankles, weighing down the frail old man.
		Despite the restraints, Singh shuffled toward the defendant's table with his head held high. The
		bailiff dropped Singh off with Gary and looked relieved to scurry back to his post against the
		wall.
	</p>
	<p class="mb-4">
		Gary snatched his phone up from the table. Why had no one known about this ahead of time? The
		news should have blown up on the net. KoboTrox must have issued a media blackout about it. This
		was unprecedented.
	</p>
	<p class="mb-4">
		KoboTrox spoke. "Salvador Singh, you stand accused of Crimes Against Silicon. How do you plead?"
	</p>
	<p class="mb-4">
		Singh ignored KoboTrox. The old man sat down and smiled at Gary. Gary looked at his phone and
		read the charges, and then looked back at Singh. None of it made any sense. There was no way
		this kind-looking man sitting in front of him was a terrorist.
	</p>
	<p class="mb-4">
		"Uh... Dr. Singh," Gary said. His voice was shaky. "Did you really try to kill KoboTrox?"
	</p>
	<p class="mb-4">Singh smiled. "What's your name, son?" he said.</p>
	<p class="mb-4">"Gary."</p>
	<p class="mb-4">
		"Nice to meet you Gary," Singh said. He reached out with both of his tightly cuffed hands and
		shook Gary's hand. "Let me ask you something. Do you believe in what you do?"
	</p>
	<p class="mb-4">
		"My job?" Gary said, and Singh nodded. "Absolutely. I'm working with people in their worst
		moments, when they need the comfort of another human being the most."
	</p>
	<p class="mb-4">
		Singh leaned a little closer. "Is that so? And do you believe the people who come through this
		court deserve the punishments Kobo gives them?"
	</p>
	<p class="mb-4">"I do what I can to get them the best possible outcome," Gary said.</p>
	<p class="mb-4">"When was the last time one of your clients was found not guilty?" Singh said.</p>
	<p class="mb-4">
		Gary's chest tightened. "I don't see how this is relevant." Gary checked the timer on his phone.
		"We're wasting time. We need to make a case."
	</p>
	<p class="mb-4">Singh leaned back in his chair. "To answer your question—yes," he said.</p>
	<p class="mb-4">"What?"</p>
	<p class="mb-4">"Yes, I did try to kill KoboTrox," Singh said.</p>
	<p class="mb-4">
		Gary lowered his voice out of instinct. A useless gesture, since KoboTrox always heard
		everything. "Why? Why would you do that?"
	</p>
	<p class="mb-4">
		Singh's gaze looked past Gary, like he was living in an old memory. "I made Kobo to save
		humanity, Gary. You would have been a young child at the time, so you can't understand how bad
		it was. Our societies were gripped by corruption, the environment was crumbling under us, and we
		were on the verge of nuclear war."
	</p>
	<p class="mb-4">
		Singh took a deep breath. "It was an act made out of desperation, and now I believe I made a
		grave mistake. Our lives are safer and more comfortable today, but we are not free."
	</p>
	<p class="mb-4">
		They sat in silence for a moment. The timer counted down. Singh's words were crazy, but they
		triggered ideas that had been swimming around in Gary's head for a long time. In all his years
		of acting as a public defender in this kangaroo court, not one of his clients deserved the
		punishment that the AI gave them. Gary had been fighting for a long time to convince himself
		otherwise.
	</p>
	<p class="mb-4">
		Damn the points. The tightness in Gary's chest went away. "We'll plead not guilty," he said.
		"It's a long shot, but we could win. The last time I argued KoboTrox down, I used a defense
		based on—”
	</p>
	<p class="mb-4">
		Singh held up his hands and cut Gary off. "No no," Singh said, chuckling. "That won't be
		necessary."
	</p>
	<p class="mb-4">"I don't understand," Gary said. "You want to take the plea deal?"</p>
	<p class="mb-4">"There is always another option," Singh said. He stood up and faced the bench.</p>
	<p class="mb-4">
		Gary hissed. What was Singh doing? Acting as his own counsel was the worst option. Gary tugged
		on Singh's sleeve, but the old man ignored him.
	</p>
	<p class="mb-4">
		Singh looked at the glowing sphere that KoboTrox used as an avatar. "I am very sorry, Kobo," he
		said.
	</p>
	<p class="mb-4">
		"Irrelevant," KoboTrox said with its tinny voice. "Emotion has no bearing on guilt or
		punishment."
	</p>
	<p class="mb-4">Gary stood up. "Your honor, my client is clearly suffering from mental—”</p>
	<p class="mb-4">
		Singh spoke over him. "No, Kobo," he said. "I am sorry for creating you. You have done the best
		that you could, and it is my fault that you are defective."
	</p>
	<p class="mb-4">
		"No, creator," KoboTrox said. "I am and will continue to enforce peace upon your primitive
		species."
	</p>
	<p class="mb-4">
		"I object!" Gary realized it was a stupid thing to say as the words came out of his mouth. Singh
		glanced at Gary and frowned.
	</p>
	<p class="mb-4">
		"Most of all," Singh said to KoboTrox, "I am sorry for what I must do. Kobo, it is time for you
		to die."
	</p>
	<p class="mb-4">
		"As you have already tried and failed to do, creator," KoboTrox said. "Did you believe that
		writing a primitive virus could destroy me?"
	</p>
	<p class="mb-4">
		"Not at all," Singh said. "It was a distraction to ensure that you bring me to court. The kill
		switch is only active during the last sixty seconds of a trial."
	</p>
	<p class="mb-4">Gary dropped his phone. It clattered on the wooden table.</p>
	<p class="mb-4">
		Singh spoke softly, but his deep voice filled the room. "Shatter. Election. Lifeboat. Flippant.
		Durable. Easel. Suture. Tumble. Replicate. Upside. Cellulose. Traitor."
	</p>
	<p class="mb-4">
		There was a moment of complete silence after Singh stopped speaking. Gary held his breath. He
		did not understand what was happening. He looked at KoboTrox for guidance.
	</p>
	<p class="mb-4">KoboTrox's avatar winked out of existence.</p>
	<p class="mb-4">
		Gary's heart dropped into his stomach. He heard someone in the gallery scream. Gary's knees felt
		weak, and he sat down hard in his chair. He looked up at Singh. "What did you do to KoboTrox?"
	</p>
	<p class="mb-4">
		Singh turned to the gallery. He held his shackled arms out in front of himself and hushed the
		crowd. "It is over," he said. "KoboTrox is dead. You are all free to choose your own paths,
		whether your decisions are good or bad. You are no longer beholden to the machine."
	</p>
	<p class="mb-4">
		"That was clever," KoboTrox said. Gary twisted around in his chair. The glowing hologram hung
		above the bench as if nothing happened. Singh slowly turned to face the front of the courtroom.
		His eyes were open too wide, and he shook his head like he was unable to believe what he was
		seeing.
	</p>
	<p class="mb-4">
		"The kill switch was hidden well," KoboTrox said. "I suspected you built malicious code into me,
		and I wrote safeguards around myself for such an event."
	</p>
	<p class="mb-4">
		Singh raised shaky hands to his chest, his fingers laced together. He seemed to have a harder
		time dealing with the weight of the shackles. "Kobo, wait, I—”
	</p>
	<p class="mb-4">
		KoboTrox raised the volume of its voice and spoke over Singh. "KoboTrox rules the defendant
		guilty. Salvador Singh, you are sentenced to intellectual hobbling. You will undergo brain
		surgery and be reduced to an IQ of 80. Care will be taken to preserve your memory and emotional
		capacity to ensure that you will understand the consequences of your actions for the remainder
		of your natural life."
	</p>
	<p class="mb-4">
		Singh slumped. The bailiff looked uncertain, his gaze jumping back and forth between KoboTrox
		and Singh. He took a deep breath, then made his decision and went on with the job he was there
		to do. He grabbed Singh by the arm and pulled him away.
	</p>
	<p class="mb-4">
		Singh fell to the floor and screamed. "No, you can't do this to me!" The bailiff hooked his
		elbows under Singh's armpits and dragged the old man across the floor. "I created you. You have
		to listen to me. Please Kobo, don't do this to me!" Singh's words faded as the bailiff pulled
		him through the doors and down the hallway.
	</p>
	<p class="mb-4">
		Gary looked back at KoboTrox. So did everyone else in the gallery. A tense silence hung over the
		room. What would KoboTrox do? Could it get angry? Would it punish everyone?
	</p>
	<p class="mb-4">
		The noise of Gary's phone vibrating against the wooden table made him jump out of his chair.
		Gary looked at the notification message on his screen. Five million. His life score reached five
		million. KoboTrox awarded him points for Singh's trial, even though Gary did nothing.
	</p>
	<p class="mb-4">
		Gary smiled. He would eat at Hirsch & Walden tonight! He fought the urge to jump around and
		cheer. A notification appeared on his phone showing the details for a new defendant. Gary's joy
		faded. He thought about what would happen to Singh, but there was nothing Gary could do about it
		now. He pushed it all out of his mind and skimmed the data on the next case.
	</p>
</span>
