	<h1 class="h2 mb-1 sm:h1 sm:mb-4">Cloned</h1>
	<h3 class="h4 mb-8 sm:h3">by Marshall Bowles</h3>

	<p class="mb-4">
		"It isn't natural," Susan said. She ran her hand across the rock face above her, feeling for a
		handhold. The warmth from the sun was in perfect balance with the crisp air. This was turning
		out to be a great day for climbing. "You're not going to be able to convince me otherwise,
		Roger."
	</p>
	<p class="mb-4">
		"I'll never understand you," Roger said from below her. Today was Susan's turn to be the lead
		climber. She and Roger did not get much time to climb anymore. The last time they were out had
		been seven months ago, and she missed it. She often daydreamed about being out on the rock while
		sitting in her cramped windowless office.
	</p>
	<p class="mb-4">
		"Cloning people, Roger!" Susan said. She found a hold and gripped it with her fingertips.
		"That's insane. It's like body snatchers. Some imposter is walking around wearing your skin and
		using your memories, but they don't have your soul."
	</p>
	<p class="mb-4">Roger laughed. "There you go again with the metaphysical talk," he said.</p>
	<p class="mb-4">Susan shook her head. "You don't have to believe," she said.</p>
	<p class="mb-4">
		"That's why I love you," Roger said. Susan was looking at the rock, but she could hear the smile
		in his voice. It made her smile too.
	</p>
	<p class="mb-4">
		They climbed in silence for a while. The ground shrank away and the wind picked up. Susan looked
		up at the vast blue sky above her, not a cloud in sight. What a day to be alive.
	</p>
	<p class="mb-4">"I'm going to invest," Roger said.</p>
	<p class="mb-4">
		Susan smiled. Roger became an unstoppable force when he had something on his mind. It was one of
		his quirks that she had gotten used to. She still loved him as much as she did when they first
		started dating. From years of experience with him, she learned it was easier to humor him.
	</p>
	<p class="mb-4">"You'll lose our money," she said.</p>
	<p class="mb-4">
		"Don't worry about that," Roger said. "I won't invest our joint savings. I'll cash out my bonds.
		And even if I lose it all—which I won't—we'll have our backup plan for our retirement."
	</p>
	<p class="mb-4">
		Susan wanted to tell him no, and it was not because of the money. Roger's long time friend
		Ganesh had graduated from MIT with a Ph.D. in bioengineering. Ganesh had a very successful
		career in the financial industry, but he had quit to get back to his roots. Ganesh approached
		Roger about investing in a startup.
	</p>
	<p class="mb-4">
		Ganesh believed he was on the verge of a breakthrough in cloning. He had spent nights and
		weekends in a makeshift lab in his tiny Brooklyn apartment. He self-funded everything using his
		ibanker salary. Ganesh was the only person she knew who could be considered a true mad
		scientist.
	</p>
	<p class="mb-4">
		Susan only knew of this second-hand through Roger. She and Ganesh weren't on speaking terms. As
		much as she wanted to think it was a hoax, she believed it was probably real. Ganesh was
		brilliant, and if he said that he would do something, he always followed through. Always.
	</p>
	<p class="mb-4">
		"Roger, I don't think it's right," she said. She knew he was prepping his argument, and she cut
		him off. "I'm being serious. It upsets the natural order."
	</p>
	<p class="mb-4">
		"If he's right and can copy your memories," Roger said with a slight quaver in his voice, "it's
		like you could live forever."
	</p>
	<p class="mb-4">
		Susan felt a lump in her throat, and she took a deep breath. So that's what this was all about.
		She suddenly felt like crying, but she held it back. Instead she let go of the rock with her
		left hand so she could pivot to look down at Roger.
	</p>
	<p class="mb-4">
		He looked up at her, and she could see the fear in his eyes. Her bout with ovarian cancer had
		changed him. The carefree boy turned more solemn. Roger tried to pretend to be that same man,
		but his fear of losing her would surface at unexpected times.
	</p>
	<p class="mb-4">"Roger," she said. "Promise me you won't ever do that to me."</p>
	<p class="mb-4">Roger opened his mouth but no sound came out.</p>
	<p class="mb-4">"Promise me," she said.</p>
	<p class="mb-4">"If I lost you, I'd—"</p>
	<p class="mb-4">
		"Roger, no." He tried to argue, but she cut him off. "No!" Her voice shook, and she turned away
		so that she would not start crying. The pain in his face was too hard to bear.
	</p>
	<p class="mb-4">
		She closed her eyes and leaned her head against the cliff face. Her helmet made a soft scraping
		sound on the rock. "It wouldn't be me," she said. "It wouldn't have my soul."
	</p>
	<p class="mb-4">
		Roger muttered something she could not hear, but he sounded angry. This had been such a good
		day, and now they were ruining it by fighting. Maybe she could still salvage it. She let go with
		her left hand and turned around. "Roger, honey, let's not fi—"
	</p>
	<p class="mb-4">
		Her right hand slipped. She had leaned back too far, her emotions clouding her judgment. She
		flailed her left hand at the wall, but she had already lost her grip. She tilted into the air, a
		slow motion fall that she was powerless to stop.
	</p>
	<p class="mb-4">
		Everything was happening too fast for her to think. Her body went into autopilot, her arms
		flapping at the air. Susan's body picked up speed as gravity took over, and she felt herself
		tumbling. She thought she heard Roger screaming.
	</p>
	<p class="mb-4">
		Her free fall lasted only a fraction of a second before her left arm slammed into a part of the
		cliff that jutted out. She felt the vibration of her bones breaking, but she did not feel any
		pain. She tried to grab the rock with her right hand, but it just scraped across the rocks and
		peeled the skin off.
	</p>
	<p class="mb-4">
		She bounced away from the cliff, more out than down because of her collision with the wall. She
		had finally gone far enough for the rope to catch her, but she was upside down when it did. Her
		momentum slammed her against the cliff and she bounced away again. Pain tore through her entire
		body.
	</p>
	<p class="mb-4">
		Susan tried to flip upright before she hit the cliff again, but her body was not responding. She
		hit and bounced off. She gave up on flipping. She rotated instead so that she was looking at the
		cliff. She reached out as she came closer to the wall.
	</p>
	<p class="mb-4">
		Her right hand brushed the rock, but she could not grab onto anything. She could not feel her
		left arm below the shoulder, and her right hand was slick with blood. She left streaks of blood
		on the granite.
	</p>
	<p class="mb-4">
		She finally was able to get a hold on a large jug, using her wrist to get enough traction to
		keep her still in one spot. It had only been a few seconds since she fell, and now she was
		upside down staring at a drop of several hundred feet.
	</p>
	<p class="mb-4">Her senses started to return, and the first thing she heard was Roger yelling.</p>
	<p class="mb-4">
		"I'm alright," she tried to say, but her voice did not work. She took a few ragged breaths and
		tried again. "I'm alright!" she yelled.
	</p>
	<p class="mb-4">
		"How bad are you hurt?" Roger said. "Can you climb back up?" He sounded scared, but she was
		impressed by how well he was holding it together.
	</p>
	<p class="mb-4">
		"No," she said. She was barely holding herself still, and the blood rushing to her head made her
		eyes feel like they were bulging out. "You'll have to climb down here and help me."
	</p>
	<p class="mb-4">"Okay," Roger said. "Hang tight. I'm coming down."</p>
	<p class="mb-4">
		She tried to calm herself by taking deep breaths. Everything will be fine. Roger would get her
		back upright, and then they would figure out how to safely get off the mountain. He was a good
		climber. He would get to her soon.
	</p>
	<p class="mb-4">
		Her harness slipped. The straps should have been able to hold her even while upside down, but
		she could feel the flat nylon sliding over her hips. Susan panicked. She let go of her hold on
		the rock and grabbed at the rope clamped to her waist. She drifted away from the wall and hung
		in midair.
	</p>
	<p class="mb-4">
		She wrapped two of her fingers around the rope, but they immediately slipped off. Susan
		tightened her abs and swung upwards, flinging her good arm. Her wrist flew wide and missed the
		rope. She only had time for one more attempt before she fell completely out of the harness.
	</p>
	<p class="mb-4">
		With all of her strength, Susan heaved her torso upwards and reached with her arm. She was too
		late. Her legs slipped through the loops of the harness, and her arm missed the rope by inches.
	</p>
	<p class="mb-4">
		She spun head over heels as she fell. The bright blue sky and ground swirled together, and she
		felt an overwhelming sadness. Roger, Roger, Roger, Roger, Roger, Roger, Roger, Roger, Roger.
	</p>
	<p class="mb-4"><br /></p>
	<p class="mb-4">-----</p>
	<p class="mb-4"></p>
	<p class="mb-4">Susan woke up.</p>
	<p class="mb-4">
		She was lying down in a large bed staring at a plain white ceiling. She felt dizzy and could not
		remember where she was. She blinked hard to try to force her brain to work, and she ran her
		hands across the soft sheets under her.
	</p>
	<p class="mb-4">
		She looked around and saw Roger. He stood a few steps away from the bed, clutching his hands
		together in front of him. He looked different, like he had aged a year. Something about the way
		he was standing made her uncomfortable, but she was too groggy to put a finger on it.
	</p>
	<p class="mb-4">
		Roger opened his mouth and made a slight motion like he was going to come closer, but he stopped
		himself. He opened his mouth to speak. His lips moved for a moment without words, and he looked
		like he was on the verge of tears. "Susan?" he said, his voice choking.
	</p>
	<p class="mb-4">
		"Roger," she said. Her voice felt odd, and she had a strange sensation that she had never used
		it before.
	</p>
	<p class="mb-4">
		Roger burst into tears and fell to his knees beside the bed. He embraced her with his muscular
		arms and buried his face in her chest. Susan started crying because he was crying. What was
		wrong? Susan could not remember anything.
	</p>
	<p class="mb-4">
		Susan wrapped her arms around him to hold him. Her arms felt weak, and she had trouble
		controlling them. She wrapped her fingers gently around the back of his neck so that her arm
		would stay steady.
	</p>
	<p class="mb-4">
		Their tears stopped but they stayed in the same place for a long time. Neither of them spoke.
		Susan tried to recall the last thing she had been doing before waking up in this bed, but her
		memories were like wet ropes slipping out of her grasp. She felt a twinge of fear. Was she sick?
		Dying?
	</p>
	<p class="mb-4">
		She pushed those thoughts away and looked around the room. It was a chic modern apartment
		decorated in a minimalist style. A large flat screen television was mounted on the wall opposite
		the bed beside the mirrored sliding doors of a closet. To her left was a pair of mahogany double
		doors that were closed. The wall to her right was clear floor-to-ceiling glass with doors
		leading to a large balcony with an amazing view of the New York skyline.
	</p>
	<p class="mb-4">
		The double doors swung open, and Ganesh walked into the room. Susan gasped. Ganesh was the last
		person she would have expected to be there.
	</p>
	<p class="mb-4">
		"Ah, you're awake," Ganesh said. He walked to the bedside and laid a hand on Roger's back.
		"Roger, I need to run some tests."
	</p>
	<p class="mb-4">
		Roger looked up into Susan's eyes. "Okay," he said, not taking his eyes off of her. Ganesh
		helped Roger to his feet and guided him to a seat at the foot of the bed.
	</p>
	<p class="mb-4">
		Ganesh took a small flashlight out of a pocket of the lab coat he was wearing. "How are you
		feeling Susan?" he said as he sat on the edge of the bed at her side.
	</p>
	<p class="mb-4">"What's happening?" Susan said. She looked to Roger.</p>
	<p class="mb-4">
		"There was an accident," Ganesh said. He flicked on the flashlight and shined it into her right
		eye. He put his thumb against her cheek to pull her eyelid down, and she jerked away out of
		reflex. She did not like Ganesh. "Come now," Ganesh said, "I need to make sure you're healthy."
	</p>
	<p class="mb-4">
		"Since when are you a doctor?" Susan said. She struggled to sit up in bed. "Roger, tell me what
		happened."
	</p>
	<p class="mb-4">
		"I lost you," Roger said. Tears rolled down his cheeks. "I couldn't get to you in time. You
		slipped out of the rope and... and..." He broke down again.
	</p>
	<p class="mb-4">
		Ganesh shook his head and smiled at Roger. "But it's all back to how it should be now, Roger,"
		he said. He gestured at Susan with a wave of his hand. "Susan is here with us and she is in
		perfect health." He frowned at Susan. "Well, I will assume it's perfect health since you will
		not let me check."
	</p>
	<p class="mb-4">
		Susan felt disoriented and a little scared. What did Roger mean about losing her? There had been
		an accident? She could not remember anything. She scrambled to get off of the bed to stand up.
		Her knees wobbled but she kept her footing. The more she moved, the more stable she felt.
	</p>
	<p class="mb-4">
		She walked around the bed and put her hands on Roger's shoulders. He looked up at her and
		wrapped his arms around her waist. "Please, Roger. Tell me what happened," she said.
	</p>
	<p class="mb-4">
		"You died," Roger said. He shuddered and held her tighter. "We were climbing. You fell. Your
		harness came off. And you fell."
	</p>
	<p class="mb-4">
		"I don't understand," Susan said. She rubbed her hand across her chest. "You mean, like
		clinically dead? A heart attack?"
	</p>
	<p class="mb-4">
		Ganesh stood up and walked around the room, stroking his beard absentmindedly. "No Susan,"
		Ganesh said. "He means you literally died. You fell from over two hundred feet and landed on a
		boulder. Your body was completely broken and you were quite, quite dead."
	</p>
	<p class="mb-4">
		Susan shook her head. "That's absurd," she said. She pointed at herself with both hands. "I'm
		standing here talking to you right now. I couldn't have died."
	</p>
	<p class="mb-4">
		"Ah, but that's where you're wrong," Ganesh said. He smiled more. "I cloned you."
	</p>
	<p class="mb-4">
		Susan's jaw dropped open and she stared at Ganesh in shock. Then she burst out in laughter. "Oh
		my God," she said. She slapped Roger on the head. "This isn't funny."
	</p>
	<p class="mb-4">
		Roger looked at her and his face showed no signs of humor. "It's true," he said. "You died and
		we brought you back to life."
	</p>
	<p class="mb-4">"I brought you back to life," Ganesh said.</p>
	<p class="mb-4">
		This was impossible. Susan grabbed Roger's arms and pulled them away from her waist. He was
		reluctant to let go, but she shoved him off. She stumbled to the mirror on the wall and looked
		at her reflection. What they were saying was completely impossible. It was absurd. But why did
		she feel like the person looking back at her in the mirror was not really her?
	</p>
	<p class="mb-4">
		Ganesh stepped up behind Susan and his reflection stared at her. "Marvelous, isn't it?" he said.
		"I've been developing the technology for years, and I'm close to going to market. You are my
		first human subject, and you are the proof that I've discovered how to cheat death."
	</p>
	<p class="mb-4">
		The realization hit Susan at once. Ganesh was not a man who joked. Ever. If he said it was true,
		then it was. Susan fell to her knees on the floor and dry heaved. There was nothing in her
		stomach, and only a thin sliver of bile spilled onto the floor.
	</p>
	<p class="mb-4">
		Ganesh knelt down and placed a hand on her shoulder. "Are you alright?" he said. "I must run
		tests on you to ensure that you are functioning properly. I'm particularly anxious to run you
		through some cognitive tests to make sure that your memories were transferred intact."
	</p>
	<p class="mb-4">
		Susan batted Ganesh away with a wordless scream. He stumbled back onto his ass and slid away
		from her. He stood up and brushed his lab coat to straighten it out. She turned to Roger. "How
		could you?" she said.
	</p>
	<p class="mb-4">Roger looked guilty. "I can't live my life without you."</p>
	<p class="mb-4">
		"Me?" Susan screamed and stood up. "Who am I? I'm not Susan. I'm—I'm a ghost walking around
		wearing a mask that looks like her." A wave of dizziness washed over her. She stayed on her feet
		out of sheer anger.
	</p>
	<p class="mb-4">
		"You're Susan," Roger said. He stood up and took deliberate steps toward her. "You're my wife. I
		proposed to you at a wobbly table in the same coffee shop where we had our first date. You said
		yes. We're going to grow old together and live on the southern coast of France when we retire."
	</p>
	<p class="mb-4">
		Roger reached out to hug Susan, but she put her hands on his chest and shoved him away. "I'm not
		that person, Roger. That person died. The thing—me—that you're talking to doesn't have a soul."
	</p>
	<p class="mb-4">Ganesh scoffed. "Please, let's have none of that religion garbage," he said.</p>
	<p class="mb-4">
		Susan turned on Ganesh. "Fuck you," she said. "You think you know everything. But you don't know
		God even though you're playing at being Him."
	</p>
	<p class="mb-4">
		Ganesh tsked. "This was always your problem, Susan," he said. "You believe in fairy tales and
		ignore the actual real science staring you in the face."
	</p>
	<p class="mb-4">
		Susan turned away. Ganesh's attitude toward her and her beliefs was the reason she had cut him
		out of her life. Or, it was the reason that the real Susan had cut him out of her life. What was
		she, this person with a dead woman's memories? She felt sick again.
	</p>
	<p class="mb-4">
		"Please keep an open mind," Roger said. He tried to hug her again, but her stare made him stop.
		"Do it for me. I can't go on without you."
	</p>
	<p class="mb-4">
		"Without who?" Susan said. "Without Susan? She's dead." She put her hand on his chest and pushed
		him hard again. She walked to the balcony doors to put the bed between her and the two men.
		"This is sick, Roger. How could you do it? You had to know that Susan would not have wanted
		this."
	</p>
	<p class="mb-4">
		Roger looked down at the floor. Susan's jaw dropped. "She told you that she didn't want this,
		didn't she?" Roger would not look up to meet her eyes.
	</p>
	<p class="mb-4">
		Ganesh held his hands together in front of him, almost like he was going to pray. "Please,
		Susan, let's take a step back from this for a moment," he said. "This is all very shocking
		information, and I'm sure we should have done a better job of breaking the news to you."
	</p>
	<p class="mb-4">
		Susan was not listening to him. Her mind was reeling with the revelation of what she was. Her
		very being was a sin against the natural order. Every moment she existed was an affront to God.
	</p>
	<p class="mb-4">
		She decided to act before she could change her mind. She turned and flung the door open. She
		could hear the men yelling as she bolted across the balcony and leapt over the rail.
	</p>
	<p class="mb-4">
		Susan tumbled head over heels as she fell through the air. She briefly wondered what had gone
		through the mind of the real Susan before the woman had died. She hit the pavement before she
		could finish the thought.
	</p>
	<p class="mb-4"><br /></p>
	<p class="mb-4">-----</p>
	<p class="mb-4"></p>
	<p class="mb-4">Susan woke up.</p>
	<p class="mb-4">
		She was in a hospital. She stared up at the white ceiling tiles from her bed, and she heard a
		monitor beeping along with the beat of her heart. She looked around and saw Roger sleeping in an
		uncomfortable looking chair beside the bed. She tried to speak but her voice croaked, almost
		like she had never used it before.
	</p>
	<p class="mb-4">
		Susan cleared her throat and tried again. "Roger," she said. Roger jerked awake. His eyes opened
		wide and he looked at Susan. Something about his gaze made Susan feel uncomfortable, but she was
		too groggy to put her finger on it.
	</p>
	<p class="mb-4">"Where am I?" she said.</p>
	<p class="mb-4">
		"You're in a hospital," Roger said. To Susan, he seemed very hesitant. "Do you remember
		anything?"
	</p>
	<p class="mb-4">
		Susan tried to think back, but her memory was fuzzy. "I'm not sure," she said. "What happened,
		Roger?"
	</p>
	<p class="mb-4">
		Roger took a deep breath. "You were in an accident," he said. "We were climbing and you fell.
		You hit your head and—" He looked up at the ceiling. "And you've been in a coma for almost a
		year."
	</p>
	<p class="mb-4">
		A year? Susan started to cry. "Oh no," she said. "Am I... Is there any permanent damage?"
	</p>
	<p class="mb-4">
		"No," Roger said. His whole body seemed to suddenly relax, and he smiled at her. "You had a head
		injury, so your memory might be funny at first. But the doctors say it should all come back in
		time." He reached out and held her hand, careful not to disturb the IV needle in her arm.
	</p>
	<p class="mb-4">"You've been here the whole time?" she said.</p>
	<p class="mb-4">Roger looked away. "Every day," he said.</p>
	<p class="mb-4">"Thank you," she said.</p>
	<p class="mb-4">
		Roger looked back at her, and it looked like he was on the verge of crying. "I don't know what I
		would do if I lost you," he said.
	</p>
	<p class="mb-4">
		"Well, I'm still here," she said, smiling. "It's going to take a lot more than a fall to kill
		me." She thought that comment would make Roger smile, but instead he looked like he was going to
		be sick.
	</p>
	<p class="mb-4">
		Susan held out her arms, and Roger leaned over to hug her. She thought about what it would be
		like to be in Roger's shoes. A spouse stuck in a coma. Wondering every day if they would wake
		up. She knew she should be happy that she had survived and she was reunited with him, but Susan
		could not shake the feeling that Roger was lying to her.
	</p>
</span>
