<span class="w-2/3 max-w-md">
	<h1 class="h2 mb-1 sm:h1 sm:mb-4">Crowds</h1>
	<h3 class="h4 mb-8 sm:h3">by Marshall Bowles</h3>

	<p class="mb-4">
		Concerts. Michelle and I loved going to them. The swaying of human bodies jammed together trying
		to get as close as possible to the stage, everyone hoping to capture some of the magic made by
		the musicians. Michelle and I were always there, side-by-side, our sweat and breath mingling
		with the rest of the people around us. The music itself didn't matter: country, rock, pop,
		metal. It was about the people around us and the energy we all created together.
	</p>
	<p class="mb-4">
		Before each show, she would look up to me with her big blue eyes and say, "Promise that you
		won't leave my side."
	</p>
	<p class="mb-4">
		And I would say, "I swear. I'll never let you go." Then I would kiss her forehead.
	</p>
	<p class="mb-4">
		Michelle was funny, pretty, and she laughed at my jokes, but she wasn't perfect. She had it
		rough growing up. Her father wasn't around, and her mother was the queen of verbal abuse.
		Michelle started using alcohol to cope when she was in high school, and she was twenty when she
		had her first DUI. By the time we met she was sober, mostly.
	</p>
	<p class="mb-4">
		She fell off the wagon four days before she disappeared. It was a Wednesday, and I came home
		from work to find the front door of our rental house unlocked and cracked open. A trail of
		clothes led from the foyer to the bedroom, where I found her naked and passed out on top of the
		covers. An empty bottle of vodka sat on the bedside table.
	</p>
	<p class="mb-4">When she woke I hugged her and she started crying. "My mom called me today."</p>
	<p class="mb-4">
		"Are you ok?" I said, gritting my teeth. Michelle's mother was a worthless old witch.
	</p>
	<p class="mb-4">
		"She asked me for money," Michelle said. "But I told her I wasn't going to give it to her
		anymore." She leaned into me and I could feel her tears on my neck. "She said she should have
		gotten rid of me before I was born."
	</p>
	<p class="mb-4">
		I held her like that for a long time, gently rubbing her hair. Her tears dried up, and she
		leaned back and looked into my eyes. "I'm so, so sorry, Mike. I needed a drink and I couldn't
		fight it. Do you hate me?"
	</p>
	<p class="mb-4">"Shh," I said and pulled her closer. "I love you. I'm not going anywhere."</p>
	<p class="mb-4">
		I took off the rest of the week from work to spend time with her. By Saturday, she was smiling
		brightly like she normally did. "Are you sure you still want to go?" I said. We had tickets to
		see Blink 182 that night.
	</p>
	<p class="mb-4">
		"Yeah," she said. She hugged me tight around my waist and pressed her head to my chest. "Thank
		you," she said softly.
	</p>
	<p class="mb-4">
		The concert was in an old warehouse that had been converted into a music hall and bar. Michelle
		and I got there an hour early, and we posted up right in front of the stage, waiting for the
		show to begin. Something in the air made me a little uneasy, but I couldn't place it. Michelle
		sensed my mood. "You okay?" she said.
	</p>
	<p class="mb-4">
		"I don't know," I said, looking around the room. "Something doesn't feel right."
	</p>
	<p class="mb-4">
		"Hey," Michelle said, smiling at me with her sparkling eyes. "Promise you won't leave my side."
	</p>
	<p class="mb-4">I smiled and kissed her forehead. "I'll never let you go."</p>
	<p class="mb-4">
		The room filled up while the opening act played, and it was packed by the time Blink took the
		stage. My discomfort only grew worse, and I felt the odd detachment from reality that only comes
		from dreams. The air turned thick and heavy, like I was swimming in mud. The people around us
		pressed in too close, and the music was wrong. Every song was in a minor key.
	</p>
	<p class="mb-4">
		I kept watching Michelle out of the corner of my eye. She was singing and dancing as always. My
		impulse was to drag her outside to where it was quiet and safe, but I didn't. I was just being
		crazy, and I didn't want to ruin her night. I held onto her hand tightly, keeping her close to
		me.
	</p>
	<p class="mb-4">
		Then Blink launched into the signature guitar riff of their song "What's My Age Again?" The
		crowd went insane, their screams louder than at any other time so far that night. Then time
		seemed to slow down. The music and voices blurred together and faded like they were far away.
		The bright lights of the stage were gone, and the ceiling was shrouded in darkness.
	</p>
	<p class="mb-4">
		Something slammed into me from behind. I stumbled forward against the sweaty back of a guy in
		front of me, and I fell down on one knee. My arm was twisted back behind me at a weird angle
		because I wouldn't let go of Michelle. Somewhere deep down I knew that I had to hold on to her.
	</p>
	<p class="mb-4">
		Michelle yanked my arm and I felt a sharp pain in my shoulder. I almost lost my grip, and she
		squeezed so hard I thought my fingers would break. I turned to look at her, but I couldn't
		understand what I saw. In my face was the chest and shoulders of a man's torso, dressed in a
		pale blue tank top. His neck extended out, and instead of a face, it was fused into the hip
		joint of someone else.
	</p>
	<p class="mb-4">
		I was surrounded by a clump of body parts that melted together like candle wax. A leg connected
		to a chest at the knee. A shoulder turned into a hip, and the skin color smoothly faded from one
		part to the next. Portions of clothing were mixed into the mass, fused to each other in the same
		random way as the bodies. Nowhere in the mix could I see a face.
	</p>
	<p class="mb-4">
		"Mike! Help me!" Michelle screamed. The jumble of bodies between us blocked my view. Her hand
		was slipping out of mine, and something was pulling her away.
	</p>
	<p class="mb-4">
		"I'm coming!" I said. I punched with my free hand, hitting any and every thing. I rammed my
		shoulder at the torso in my way. Its skin and muscle absorbed my blows like firm pillows, but it
		didn't move. I placed my foot on a hairy knee growing out of a neck and craned up to look over.
	</p>
	<p class="mb-4">
		Michelle was on the other side, desperately clinging to my hand. The mass of bodies behind her
		bent together to form some kind of tunnel close to the ground. One of Michelle's legs stretched
		back into that hole, like something had her, but it was too dark down there to see.
	</p>
	<p class="mb-4">
		Michelle looked up and our eyes met. Tears ran down her cheeks. "Don't let me go."
	</p>
	<p class="mb-4">
		A slender, gray tentacle emerged from the darkness of the tunnel. It snaked up around Michelle's
		waist. She looked down when she felt it wrapping around her and screamed. I pulled on her hand
		with all my strength, but it was pointless. In one swift motion, the tentacle yanked Michelle
		away and down in the darkness.
	</p>
	<p class="mb-4">
		"No!" I yelled. I crawled over the bodies toward the hole, punching the flesh in my path. In my
		haste, I put my foot in the wrong spot and slipped, falling face first into the floor. I blacked
		out for just a moment, and when I opened my eyes four guys were holding me down on the floor.
		One of them had a bloody nose.
	</p>
	<p class="mb-4">
		"Get off of me," I yelled. I struggled against them, but I couldn't move. They held me down
		until two huge bouncers arrived, and I was hauled kicking and screaming into the alley behind
		the building. "You have to let me back in," I said. "Something took my girlfriend."
	</p>
	<p class="mb-4">
		The cops showed up and I tried to tell them what happened, but of course they didn't believe me.
		Who would? They figured I got drugged out of my mind, started attacking people at the concert,
		and that Michelle must have ditched me. They locked me up for the night. I wanted to believe
		them. What I saw wasn't possible. I convinced myself that the cops were right, and I held onto
		the thought that it was all just a bad dream.
	</p>
	<p class="mb-4">
		I got out the next day and took a bus home, but Michelle wasn't there. All of my calls went to
		her voicemail, and she didn't answer any of my texts. I went back to the police station and
		tried to file a missing person's report, but they just threatened to lock me back up again. I
		grew more desperate with each passing day. When the cops finally realized that Michelle was
		actually missing, they pegged me as the main suspect. But with no evidence and no leads, they
		finally gave up and quit looking.
	</p>
	<p class="mb-4">
		The most important person in my world was gone. I quit showing up to work and eventually got my
		termination notice in the mail. My days were spent in the apartment staring at the empty places
		where Michelle should have been. Her head resting on the pillow on her side of the bed. Michelle
		standing in my way in front of the sink when we were both trying to get ready in the morning.
	</p>
	<p class="mb-4">
		One day I got a good look at myself in the mirror. I had not shaved in weeks, and I couldn't
		remember the last time I showered. Michelle would have been disappointed in the sad sack that I
		became. Then I felt a burning anger rising up from my gut. Something took her from me, some
		monster killed her. I made a promise to myself that I would find out what it was, and I would
		kill it.
	</p>
	<p class="mb-4">
		For two years I combed through every missing persons report I could find, looking for anything
		remotely similar to what happened to her. The details were always sketchy, and I had to do a lot
		of reading between the lines. I believed the key was to focus on cases of people who suddenly
		disappeared in crowded, public spaces.
	</p>
	<p class="mb-4">
		I emailed a woman in Seattle who lost her boyfriend during an environmental protest. He was a
		former heroin addict who was working hard to get clean. They were standing together on the
		street holding signs. She looked away for a moment, and he was gone when she turned around. She
		never saw him again.
	</p>
	<p class="mb-4">
		There were others. A guy in Kentucky lost his brother while they were at the track, betting on
		the races. A man's teenage son disappeared from beside him in the stands at a college football
		game. A woman's mother vanished at a country music concert. A man's husband was taken from him
		in Times Square on New Year's Eve.
	</p>
	<p class="mb-4">
		All of the victims were addicts at one point, but they had beaten their habits. In the days
		right before they disappeared, each one of them had relapsed. It always happened in a public
		place with plenty of potential witnesses who never saw anything. With that theory, I came up
		with a plan.
	</p>
	<p class="mb-4">
		I volunteered at a rehab facility, and that's where I met Chelsea. She was a recovering meth
		addict, emotionally vulnerable, and in desperate need of a boyfriend. She slept with me the same
		night we met, and we started dating right away. She was the perfect bait.
	</p>
	<p class="mb-4">
		She relapsed in the second month we were dating. I found her at the apartment of one of her
		trashy friends. She was tweaked out of her mind. Chelsea fought against me, but I hauled her out
		of there and told her it was for her own good. I locked her in my bedroom for two days.
	</p>
	<p class="mb-4">
		She actually thanked me when I let her out. I felt a pang of guilt, but I brushed it aside.
		Chelsea was my key to finding out what happened to Michelle. As a "reward" for her being so
		strong, I bought tickets to an EDM concert that weekend. She was really into that kind of crap.
	</p>
	<p class="mb-4">
		On that Friday night, we stood in a crowd of people jammed close together near the stage. They
		were scantily clad, sweaty, and dancing with the rhythm of deep base coming from the speakers. I
		held onto Chelsea's hand tightly, and she looked at me and smiled. She yelled something to me
		over the music. I think it was, "I love you."
	</p>
	<p class="mb-4">
		It happened when the main act, Vorpal Geyser, came on stage. The crowd went wild, and everything
		started moving in slow motion. I looked around and saw that I was no longer surrounded by
		people. They had been replaced by the mass of body parts melted together. I still held Chelsea's
		hand, but she was being pulled away. I dropped to my knees, rammed my shoulders into the
		creature, and crawled across the concrete floor following Chelsea as she was pulled away.
	</p>
	<p class="mb-4">
		My hand touched something soft. It was a bicep, and at the crook of the elbow it connected to an
		ankle. I was in a tunnel made of disjointed body parts of the creature. It was dark, and I could
		barely see Chelsea's face even though I still held her hand. She screamed for help, but her
		voice sounded so far away.
	</p>
	<p class="mb-4">
		What was I doing? This was madness. I should have never allowed myself to go this far, using
		another human being as bait. Even though I couldn't see them, I knew there were tentacles
		wrapped around Chelsea. I tried to pull her back anyway, but just like with Michelle, she was
		snatched out of my grasp.
	</p>
	<p class="mb-4">
		I followed, scrambling down the tunnel until it ended in a steep drop at the edge of a giant
		cavern. The walls stretched away into the distance, every surface made of writhing human parts.
		A pale mist hung in the air below, obscuring the ground. The stalk of a giant gray mushroom rose
		out of the mist. It was as thick and as tall as a skyscraper, and its surface was covered in
		small twitching white specks.
	</p>
	<p class="mb-4">
		Chelsea was carried through the air by a long tentacle that grew directly out of the mushroom
		stalk. She was beyond my help now, and I could only listen to her faint screams. The tentacle
		pulled her into the main body of the mushroom creature. She kicked and slapped the surface, but
		her hands and feet stuck to it like hot tar.
	</p>
	<p class="mb-4">
		The mushroom skin oozed over Chelsea, absorbing her until only her face remained. Her skin
		slowly turned to the same gray color as the mushroom stalk, and only her eyes remained. Oh god,
		those eyes. All of the tiny, flitting specks covering the creature were human eyes.
	</p>
	<p class="mb-4">
		Then out of the thousands covering the monster, I was drawn to one pair of deep blue eyes.
		Michelle. She saw me, and I could sense that she recognized me. In that moment I felt her pain
		and fear, and the weight of the lost life that we would never share. She was trapped in eternal
		torment with no hope of redemption. I screamed.
	</p>
	<p class="mb-4">
		Her eyes looked at me with a sense of urgency. She wanted me to go, to get away from this
		hellish place and back to safety. I looked at her one last time and mouthed the words, "I love
		you." Then I turned away like a coward and crawled out. The tunnel shrank as I went, the walls
		closing in and threatening to crush me. I wormed my way out through the last few feet, the exit
		by then barely wider than my shoulders.
	</p>
	<p class="mb-4">
		I fell out onto the concrete floor back in the real world, my empty heart beating from the
		exertion. Vorpal Geyser launched into another song. I knelt there on the floor and cried. It
		would have been better if Michelle was dead. I wished I was dead. Someone eventually grabbed my
		arm and guided me out of the crowd.
	</p>
	<p class="mb-4">
		I tore my rotator cuff holding onto Chelsea, although I didn't feel it at the time. The doctor
		gave me a prescription for Vicodin to help with the pain, and it was easy to keep taking it even
		after I was physically recovered. I wanted it to help me forget, to take away the emotional
		pain. It took me a year to quit.
	</p>
	<p class="mb-4">
		I should feel guilty for what I did to Chelsea—and I do, a little—but mostly my thoughts are
		consumed with Michelle. I can't stop seeing her tormented eyes, my beautiful girlfriend absorbed
		as part of that creature. I promised I would never leave her side.
	</p>
	<p class="mb-4">
		Mary is a single mother of two, and she's never broken a rule in her life. We met online and
		started dating not long after. Earlier this week, I intentionally relapsed using the bottle of
		Vicodin I had secretly stashed away. Mary took off work and stayed with me for a couple of days
		to make sure I got clean.
	</p>
	<p class="mb-4">We're going to a concert this weekend.</p>
</span>
