<span class="w-2/3 max-w-md">
	<h1 class="h2 mb-1 sm:h1 sm:mb-4">12th Ave NW, Unit M</h1>
	<h3 class="h4 mb-8 sm:h3">by Sara Lufrano</h3>

	<p class="mb-4">He found her sitting on the couch in the dark. “What are you doing awake?”</p>
	<p class="mb-4">“I couldn't sleep. Do you want to sit with me?”</p>
	<p class="mb-4">
		“No.” He didn't find the couch comfortable but she liked it. "I want to lie down.”
	</p>
	<p class="mb-4">“Okay. Well, I'll be here.”</p>
	<p class="mb-4">
		“You'll be here all night?” He leaned against the wall, rubbed his eyes, and yawned.
	</p>
	<p class="mb-4">“Probably. I'm not tired anymore.”</p>
	<p class="mb-4">“It's only been four hours.”</p>
	<p class="mb-4">“I napped earlier. I'm fine,” she said.</p>
	<p class="mb-4">“Were you playing music or singing or something?” he asked.</p>
	<p class="mb-4">She shook her head.</p>
	<p class="mb-4">“Okay. I'm going back to bed.”</p>
	<br />
	<br />
	<p class="mb-4">
		He lay there in bed, tightly holding his eyes closed. He was so tired. He thought he heard
		humming every night. It was a nonsensical song that he heard when he closed his eyes but
		couldn't hear when he listened for it.
	</p>
	<p class="mb-4">
		His alarm went off. He wanted to yell into his pillow but instead lay there motionless hoping
		that the clock was wrong.
	</p>
	<p class="mb-4">
		She leaned in the room holding onto the doorway. “I can make breakfast. How about that?”
	</p>
	<p class="mb-4">
		He rubbed his eyes and turned off his alarm. “Sure, sure that sounds great. Coffee?”
	</p>
	<p class="mb-4">“It's ready. Want me to pour you a cup?”</p>
	<p class="mb-4">
		He nodded as he got out of bed. She stayed in the doorway and they kissed when he walked by.
	</p>
	<p class="mb-4">“Do you feel okay?” he asked, walking to the dining area of the living room.</p>
	<p class="mb-4">“Yes.” She smiled.</p>
	<p class="mb-4">“I'm worried about you being up all the time.”</p>
	<p class="mb-4">“I'm fine.”</p>
	<p class="mb-4">“Then I just miss you in bed.” He smiled.</p>
	<p class="mb-4">She smiled.</p>
	<p class="mb-4">
		He sat at the two-person table under the window and opened his laptop to check the news.
	</p>
	<p class="mb-4">
		She walked through to the kitchen to start breakfast. He heard her humming. He slammed his
		laptop closed as he realized that it was her humming keeping him up at night.
	</p>
	<p class="mb-4">He rushed to the kitchen and stared at her. “You hum that at night.”</p>
	<p class="mb-4">“You can hear me?”</p>
	<p class="mb-4">
		“Yes. I didn't think it was you. I thought I was dreaming it or that it was the neighbors.”
	</p>
	<p class="mb-4">She frowned. “I'm sorry.”</p>
	<p class="mb-4">
		A fear of losing another night of sleep flared in him and he heard the humming in his mind. He
		needed her to sleep, or for her to leave, or he needed to get out.
	</p>
	<p class="mb-4">But all he could say was, “I don't want to wake up without you.”</p>
	<p class="mb-4">“I'll try,” she said.</p>
	<br />
	<br />
	<p class="mb-4">
		He snapped awake in the dark. She wasn't next to him. He heard her in the living room, pacing
		and humming. He got up and went to her. She turned to him and smiled.
	</p>
	<p class="mb-4">“Hi,” she said.</p>
	<p class="mb-4">“Why can't you sleep?”</p>
	<p class="mb-4">
		She stared wide-eyed and shook her head. His shoulders fell and he went to her, hugged her. He
		leaned his head on her head.
	</p>
	<p class="mb-4">“What's wrong with you?” he asked.</p>
	<p class="mb-4">“I feel fine.”</p>
	<p class="mb-4">
		“Just because you feel fine doesn't mean that you are,” he said. “What is happening?”
	</p>
	<p class="mb-4">She shook her head.</p>
	<p class="mb-4">
		He sighed. “Come back to bed, yeah?” He rubbed his hands slowly up and down her back.
	</p>
	<p class="mb-4">
		She nodded and he led her by the hand. They lay down, holding hands. His eyes closed only for a
		few moments before he heard the humming. He looked at her. She was quiet. She looked peaceful.
		He stayed awake watching her, waiting for her to begin humming.
	</p>
	<p class="mb-4">
		She was out of bed again when he woke. He didn't know when he fell back asleep. She was sitting
		at the table, wrapped in a blanket looking out the window.
	</p>
	<p class="mb-4">“Is it raining?” he asked.</p>
	<p class="mb-4">“Yes. Good morning. Was I making noise?”</p>
	<p class="mb-4">“Could have just been the rain.”</p>
	<p class="mb-4">He sat down with her.</p>
	<p class="mb-4">“How long have you been doing this?” he asked.</p>
	<p class="mb-4">“I don't know,” she said. “I haven't lived with anyone for a long time.”</p>
	<p class="mb-4">
		“I worry about it. And you. I can't sleep and I feel like shit. You have to be feeling the
		same.”
	</p>
	<p class="mb-4">“I feel fine,” she said.</p>
	<p class="mb-4">He shook his head. “I don't believe that.”</p>
	<br />
	<br />
	<p class="mb-4">
		It had been three weeks since he had a full night's rest. Now his anger got in the way of caring
		and he let her hum and pace, all night, every night by herself. Nothing he said or suggested
		changed her behavior.
	</p>
	<p class="mb-4">Every night he thought about leaving. Get up and go, leave her.</p>
	<p class="mb-4">
		He thought about how he needed to break up with her. The reason was obvious to him. He knew she
		would cry. That would hurt him. But there was nothing else he was willing to do for her.
	</p>
	<br />
	<br />
	<p class="mb-4">
		“I'm sorry,” she said as they were sitting on the couch after dinner. His eyes were wide,
		staring at the TV.
	</p>
	<p class="mb-4">“Why?”</p>
	<p class="mb-4">“You're not sleeping because of me.”</p>
	<p class="mb-4">He didn't respond.</p>
	<p class="mb-4">“I'm sorry.”</p>
	<p class="mb-4">He didn't respond.</p>
	<p class="mb-4">“Maybe I'll go stay at a hotel for a bit,” she said.</p>
	<p class="mb-4">He sighed. “It's your apartment. I'll go.”</p>
	<p class="mb-4">“I'm sorry,” she said.</p>
	<br />
	<br />
	<p class="mb-4">
		It had been two weeks after his hotel stay and gradually she was able to stay quiet most of the
		night. He didn't know why or care how she was able to do it. Now he didn't lie down at night
		worrying about not being able to sleep. She still rose early but didn't disturb him enough to
		wake him.
	</p>
	<p class="mb-4">“Morning.” He joined her in the kitchen and kissed her neck.</p>
	<p class="mb-4">“Morning.”</p>
	<p class="mb-4">“Did you sleep well?”</p>
	<p class="mb-4">She nodded.</p>
	<p class="mb-4">“Good. Me too.”</p>
	<p class="mb-4">She fumbled putting the coffee pot back in the maker.</p>
	<p class="mb-4">“You okay?” he asked grabbing her hand.</p>
	<p class="mb-4">
		“I went to the doctor while you were gone,” she said. “She gave me medication to help me sleep
		and control the humming.”
	</p>
	<p class="mb-4">“I knew there was a reason why. That's great.” He pulled her into himself.</p>
	<p class="mb-4">“My brain is also deteriorating as if I'm in the early stages of dementia.”</p>
	<p class="mb-4">His grip on her didn't change.</p>
	<p class="mb-4">“What does that mean?” he asked.</p>
	<p class="mb-4">“I think I'm going to die.”</p>
	<br />
	<br />
	<p class="mb-4">
		Now he stayed up most nights watching her sleep, making sure she was breathing. He couldn't
		sleep for the thought of her dying, lying next to him.
	</p>
	<p class="mb-4">
		While he watched her he thought about the first night he'd be able to sleep as much as he
		wanted. The night that she wouldn't be there, he'd be able to sleep.
	</p>
	<p class="mb-4">
		He knew he should be sad but he wasn't. As the nights went on he turned impatient.
	</p>
	<br />
	<br />
	<p class="mb-4">
		She died about a month later. He met her mom and dad while he moved his things. They gathered
		small possessions. Whatever they needed to remember her by.
	</p>
	<p class="mb-4">
		Her mom offered a framed picture of her for him to take as a keepsake. He refused it. He didn't
		need it. He had memories, enough of them, he told her mom when her face turned sour.
	</p>
	<p class="mb-4">
		He left, drove to his new apartment and unpacked. There was no bed yet but he knew he could
		sleep.
	</p>
