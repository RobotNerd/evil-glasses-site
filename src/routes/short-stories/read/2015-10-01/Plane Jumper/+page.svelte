<span class="w-2/3 max-w-md">
	<h1 class="h2 mb-1 sm:h1 sm:mb-4">Plane Jumper</h1>
	<h3 class="h4 mb-8 sm:h3">by Marshall Bowles</h3>

	<p class="mb-4">
		Donald was born on a Sunday in the summer. His parents had planned it well. There was no mad
		rush to the hospital. The epidural came right on time. His mother went through very little pain.
		Baby Donald popped out into the waiting arms of the doctor. A normal, easy birth to what seemed
		to be a normal, healthy boy.
	</p>
	<p class="mb-4">
		But Donald was not normal. He had a gift. That's what his grandmother told him, for as long as
		he could remember. "Your gift is rare," she said when he was five years old. Donald was at her
		house and pulling weeds out of the flowerbed while grandmother swept the porch. She made him do
		chores whenever he visited her. "It's disappearing from this world. Everyone I know who had it
		is dead now, except you and me."
	</p>
	<p class="mb-4">"Are you sure I have it?" Donald said.</p>
	<p class="mb-4">
		"Yes, sweetie," his grandmother said. "Sensing the power in others is a small piece of the
		gift."
	</p>
	<p class="mb-4">
		She made him promise to keep it a secret, which was hard for Donald. He desperately wanted to
		brag to his parents about how special he was supposed to be. He stood by his mother's side while
		she stirred vegetable soup in the big black pot, rocking back and forth on his feet. "Nana said
		I'm going to have adventures!" he wanted to say. "She says I can do stuff nobody else can." But
		he kept it to himself, the special secret just for him and his grandmother.
	</p>
	<p class="mb-4">
		"They'll think you're crazy, Donald," his grandmother said. "No one will believe you if you tell
		them." She flipped through the channels on TV, skipping past the cartoons that Donald wanted to
		watch.
	</p>
	<p class="mb-4">
		"Why?" Donald asked her. He laid on the shag carpet of the living room floor with a couch pillow
		under his chest.
	</p>
	<p class="mb-4">
		"Because they don't understand," she said. She flipped to a nature show about bears. She stayed
		on that channel long enough for Donald to get excited, and then she changed channels again.
		"They can't see what I see, and what you will see when your gift is ready. People don't believe
		in what they can't see."
	</p>
	<p class="mb-4">"But what about Jesus," Donald said. "They can't see him."</p>
	<p class="mb-4">His grandmother laughed. She flipped to the news channel and stopped.</p>
	<p class="mb-4">
		Donald stared off in the distance, ignoring the images playing on the television. He imagined
		the adventures he would go on. He would be the first person to explore places where no other
		humans had ever been.
	</p>
	<p class="mb-4">
		Donald tossed a baseball with his father in the yard behind their two-story house. "Good catch,"
		his father said. "Do it just like that in the game on Thursday." Donald tossed the ball back. He
		wondered if baseball existed in those other worlds. He made a mental note to ask his grandmother
		about it later.
	</p>
	<p class="mb-4">
		"Nana, how will I know when I'm ready?" Donald said. He dipped a hard-boiled egg into a dish
		full of blue dye. The basket on the table was halfway full of the eggs he had finished.
	</p>
	<p class="mb-4">
		"You'll just know," his grandmother said. She rinsed the soap off of a plate and put it on the
		drying rack. She reached into the soapy water in the sink and grabbed a coffee mug. "It will
		happen suddenly, and it will be different than anything you've ever felt before in your life."
	</p>
	<p class="mb-4">
		"Will it be soon?" Donald said. He used tongs to roll the egg around in the bowl.
	</p>
	<p class="mb-4">
		"I don't think so, honey," she said. "You're only eight. It happens when you become an
		adolescent. I was ten at my awakening, but boys tend to mature slower. So probably not until
		you're twelve or thirteen."
	</p>
	<p class="mb-4">"Aw," Donald said. "That's forever."</p>
	<p class="mb-4">
		His grandmother laughed. "If you want to do something," she said, "you can practice your
		concentration exercises."
	</p>
	<p class="mb-4">"Those are boring," Donald said.</p>
	<p class="mb-4">
		"They're important," his grandmother said. "You'll need to be good at them to use your gift."
	</p>
	<p class="mb-4">
		"Oh fine," Donald said. He sat down on the kitchen floor, crossed his legs, and closed his eyes.
		He slowed his breathing like his grandmother had taught him, and he tried his best to listen to
		his body. Donald had trouble understanding what his grandmother meant for him to do, but he
		tried the best he could.
	</p>
	<p class="mb-4">
		Donald stared out the window at the pine trees swaying in the wind. He should have been focused
		on finishing his math assignment, like the rest of his classmates. They hunched over their
		desks, furiously scribbling down numbers. Donald wondered if trees were the same in other
		worlds.
	</p>
	<p class="mb-4">
		"Donald!" Ms. Hemsworth said. "Quit daydreaming and do your work." Donald frowned and looked
		back at the paper on his desk.
	</p>
	<p class="mb-4">
		"How can I come back and forth between here and those other places?" Donald said. His
		grandmother looked up from the tablecloth in her hands. The red thread stretched tight between
		the needle and the partially repaired rip in fabric.
	</p>
	<p class="mb-4">
		"Donald, I've already told you a hundred times," she said. His grandmother was doing the thing
		where she pretended to be annoyed with Donald. He knew she was trying not to smile at him.
		"Anything I tell you won't make sense. I have to show you."
	</p>
	<p class="mb-4">
		Donald stood up from the table with the half-finished jigsaw puzzle, unmatched pieces spread out
		across the polished wood. He bounced over to his grandmother. "Then do it now," Donald said. "Go
		somewhere! I'll watch you, and maybe I will be able to see it."
	</p>
	<p class="mb-4">
		His grandmother shook her head and set aside her sewing. She smiled softly. "You've watched me
		jump before."
	</p>
	<p class="mb-4">"Pleeeeeease," Donald said. "I'm older now."</p>
	<p class="mb-4">"All right, dear," she said. "But it won't do any good."</p>
	<p class="mb-4">
		Donald held onto her hands. He could barely contain his excitement. Every time his grandmother
		made a jump, she would come back with fantastical stories. Each time was new and different.
		"Where are you going?" Donald said. "The fairy land? The world of flying giants?"
	</p>
	<p class="mb-4">
		His grandmother tapped her chin with a gnarled finger. She was stronger than her gray hair and
		wrinkled skin made her appear. "Hm," she said. "What about the place with the purple crystals
		that sing?"
	</p>
	<p class="mb-4">Donald shrugged. "Yeah, I guess so," he said. "That one is kind of boring."</p>
	<p class="mb-4">
		"It wouldn't be boring if I took you there," she said. His grandmother looked at him for a long
		moment. She bit her lip, and Donald knew she did that when she was trying to make up her mind
		about something. "What if I bring something back with me? Something from that place?"
	</p>
	<p class="mb-4">"Really?" Donald said.</p>
	<p class="mb-4">
		"Yes," she said. "We can't keep it here for long, but I think a few minutes will be okay."
	</p>
	<p class="mb-4">"Yes!" Donald said.</p>
	<p class="mb-4">
		"Okay," his grandmother said. She laid back in her recliner, closed her eyes, and started taking
		slow, deep breaths. Donald could feel her heartbeat pulse through the soft skin of her hand. He
		tried to match her breathing, and he tried to feel her with his heart. He hoped he would sense
		some spark of energy when she jumped, and then he would be able to jump on his own.
	</p>
	<p class="mb-4">
		Her body suddenly tensed, and her eyes opened wide. Donald expected this, because of the many
		times he had watched her jump in the past. Deep black pits of emptiness filled the place where
		her eyes should be. Looking into that darkness made Donald feel like he was looking down into an
		old well where it was impossible to see the bottom because there was no bottom.
	</p>
	<p class="mb-4">
		Just as soon as it began, it ended. His grandmother's body relaxed. She blinked, and her eyes
		were back where they should be. She looked at Donald.
	</p>
	<p class="mb-4">"Well?" she said. "Did you feel anything?"</p>
	<p class="mb-4">"I think so," Donald said.</p>
	<p class="mb-4">
		His grandmother laughed. "You wouldn't answer that way if you had." Donald pouted. "Don't you
		want to see what I brought back?"
	</p>
	<p class="mb-4">
		Donald forgot about his disappointment. "Yes!" His grandmother pointed to the coffee table
		behind him. Donald whirled around. Right in the middle of the table was a large purple crystal.
		A soft light flickered in its center. Donald slowly stepped toward it. He reached out but
		stopped short. "Can I touch it?" he said.
	</p>
	<p class="mb-4">"Yes, honey," his grandmother said. "It's safe."</p>
	<p class="mb-4">
		Donald touched the crystal with his pointer finger. When he did, it hummed. Donald giggled. It
		reminded him of the sounds that the hummingbirds made while they fought over the feeder his
		parents put out in the summertime.
	</p>
	<p class="mb-4">"Is it alive?" Donald said.</p>
	<p class="mb-4">
		"Not in the way that you and I think of things being alive," his grandmother said. "But it has
		thoughts and feelings, so I'd say that's close enough."
	</p>
	<p class="mb-4">
		"Can I keep it?" Donald said. "I won't show anyone, I promise. You know I'm good at keeping
		secrets."
	</p>
	<p class="mb-4">
		"No," she said. Donald started to argue, but she cut him off. "It's not because I think you
		would tell anybody."
	</p>
	<p class="mb-4">"Then why not?" Donald said.</p>
	<p class="mb-4">
		"It doesn't belong in our world," his grandmother said. "You saw how I never went fully across
		to the other place, because my body stayed here the whole time."
	</p>
	<p class="mb-4">
		His grandmother picked up the crystal from the table. Its hum was different when she touched it,
		deeper and slower. "I've never brought something across," she said, "but I wanted you to see
		something with your own eyes. Its body is here completely, and it doesn't belong."
	</p>
	<p class="mb-4">
		Donald started to tear up, and his grandmother gently patted him on the head. "Don't cry
		sweetie," she said. "In a few years I'll take you where this crystal comes from. You and I will
		go together. I promise."
	</p>
	<p class="mb-4">
		She leaned back in her recliner. Donald watched the crystal in her hands when she made the jump.
		One moment it was there, and the next it was gone. Vanished in the blink of an eye.
	</p>
	<p class="mb-4">
		His grandmother opened her eyes and sat up in the chair. "Nana," Donald said. "How did you do
		that? You just sat here the whole time and didn't move."
	</p>
	<p class="mb-4">"Because when I jump," she said, "I'm in both places at once."</p>
	<p class="mb-4">
		Donald's grandmother died on a Sunday in the summer. It was his thirteenth birthday. She was
		driving her car to Donald's house, his birthday present neatly wrapped up in the trunk. A truck
		driver fell asleep at the wheel, crossed the center line, and hit the driver's side of her car
		at full speed. Donald was helping his mother clean the house when the police knocked on their
		door.
	</p>
	<p class="mb-4">
		The church pews were full, forcing some of the men and women dressed in their mourning attire to
		stand against the walls. Donald sat in the front row, sandwiched between his parents. He had so
		far kept himself from crying in front of others, and he fought the tears back now. Donald wanted
		to leap out of his body, leap out of existence. Why was this happening? He ground his teeth to
		stop his lip from quivering. Why couldn't things go back to the way they were a few days before?
	</p>
	<p class="mb-4">
		The priest stood at the podium, towering over the polished oak coffin where Donald's grandmother
		lay. The priest opened a massive Bible and read scripture, but to Donald the words were an
		unintelligible sound droning in the background. Donald started shaking, and his mother reached
		over to grip his hand tightly.
	</p>
	<p class="mb-4">
		Donald looked down at his lap and tried not to cry. He squeezed his eyes tightly shut. He slowed
		his breathing. No matter how hard he tried, he could feel the tears rising up from his chest. He
		bit his lip and tried to force them down. Donald wanted to scream. Why did you leave me Nana?
	</p>
	<p class="mb-4">
		Donald felt a draft of freezing cold air brush against his face. His mother was no longer
		holding his hand, but he did not remember her letting go. He realized that the priest had
		stopped talking, and the room was unnaturally silent. There were hundreds of people at his
		grandmother's funeral, but they made no noise. Something was wrong.
	</p>
	<p class="mb-4">
		Donald opened his eyes. He was in a church, but it was not the same church. The warm wooden
		walls were now pale concrete, stretching up high to a shadowy vaulted ceiling. There were no
		stained glass windows, but large clear windows holding back heavy gray mist. The pew on which
		Donald sat was burnt black wood.
	</p>
	<p class="mb-4">
		The podium and the priest were gone. A flat concrete alter stood at the front of the chapel.
		Resting in the middle of the alter was a fleshy lump. Donald stared at it for a moment before
		his mind could understand the shape. It was a human eye the size of a watermelon.
	</p>
	<p class="mb-4">
		The eye twitched. Tentacles extending from the back of the disembodied eye flopped like a fish
		out of water, in its last desperate moments of life. Donald gasped. Stale air filled his lungs,
		carrying the scent of a coffin buried deep underground.
	</p>
	<p class="mb-4">
		Donald realized there were others in the pews with him, though no one close by. To his right,
		several feet away, sat a hunched figure covered completely in black. The woman wore a dress and
		a thick veil hung down over her face. No part of her body was visible. Even her hands were
		covered by black gloves. Her head tilted downward, so that she must have been staring at her lap
		through the veil.
	</p>
	<p class="mb-4">
		Donald looked over his shoulder. Feminine figures almost identical to the one beside him
		sparsely filled the pews in the large sanctum. Every single one wore the same black clothing,
		and they were all the same height and build. Every single one of them stared at its own lap.
	</p>
	<p class="mb-4">
		Donald heard a creaking sound from the front of the church. He jerked his head back around in
		time to see a large man enter the church through an arched doorway behind the alter. The man had
		to be at least seven feet tall, and he wore thick red robes that were long enough to drag along
		the floor. His face was covered by a mask that looked like a goat, except the mask split at the
		bridge of the nose down to the snout, much like a forked snake's tongue.
	</p>
	<p class="mb-4">
		The man carried a dark red staff in one hand and long slender blade in the other. He approached
		the alter with his arms raised above his head and his head looking up to the ceiling. Donald was
		petrified. Something about the man in the robes terrified him so badly that he was too afraid to
		shiver.
	</p>
	<p class="mb-4">
		The man stopped at the alter. The forked mouth of his mask started moving, and the creaking
		sound intensified. That was when Donald realized that the man was not a man, and it was not
		wearing a mask. The creaking sound was its voice.
	</p>
	<p class="mb-4">
		The creature looked down at the disembodied eye on the alter in front of it. It gestured towards
		the eye with the weapons in its hands, and it spoke more of its unintelligible creaking speech.
		The creature raised its arms high and looked out across the black-clad congregation. That's when
		it noticed Donald.
	</p>
	<p class="mb-4">
		The creature stopped speaking, clearly surprised to see Donald. For a long moment, Donald stared
		into those strange goat-like eyes. The creature's arms were frozen in the air, the wet nostrils
		on its forked snout quivering. Donald could not breathe.
	</p>
	<p class="mb-4">
		The creature broke the silence with a scream that sounded like metal grating against metal.
		Donald felt like he was in his grandmother's car and the speeding freight truck was running over
		him. From the corner of his eye, Donald saw the black-clad woman closest to him raise its head.
		Donald turned toward her, and he could see the others in the congregation looking, all staring
		at him.
	</p>
	<p class="mb-4">
		The woman closest to Donald raised her hands and pulled aside her veil. Donald's hands started
		shaking when he realized that this was not a woman – at least, not a human one – because she did
		not have a face. Its pale lumpy skin was shaped like a human head, but there were no eyes, nose,
		nor hair. A wide mouth stretched from ear to ear. The thing opened that mouth and shrieked,
		baring multiple rows of pointed teeth.
	</p>
	<p class="mb-4">
		Donald tried to stand up, but he stumbled and fell flat on his face. His legs were paralyzed out
		of fear. He heard rustling fabric and could see the dress of the mouth-faced thing coming closer
		to him. Donald started hyperventilating. He tried to get his hands under himself so that he
		could stand. The thing was almost on top of him, and he knew he would not get away. All he could
		hear was the sound of his own heart beating.
	</p>
	<p class="mb-4">
		Donald closed his eyes and screamed. He felt hands grab his shoulders, and he screamed louder.
		The thing grabbing him flipped Donald over onto his back, and he heard it scream. "Donald!
		Donald!" It was his mother's voice.
	</p>
	<p class="mb-4">
		Donald opened his eyes and looked up into his mother's face. Both of his parents were kneeling
		over him, and his mother was holding him tightly by the shoulders. He was lying on his back at
		the front of the church. The priest looked down on him from behind the pulpit.
	</p>
	<p class="mb-4">
		Donald cried, his tears a mix of fear and relief. His father picked him up, and Donald buried
		his head in his father's chest. He sobbed into the wool suit coat as he was carried out of the
		church and gently placed in the back of the car. The ride home was silent.
	</p>
	<p class="mb-4">
		His parents assumed that the episode at the church was anguish from his grandmother's death, and
		Donald did nothing to make them believe otherwise. They finally tucked him into bed and left him
		alone. The fear that he would get sucked back into another reality kept him up all night.
	</p>
	<p class="mb-4">
		His next jump happened a few days later during history class. His teacher was talking about the
		Battle of the Bulge, and Donald was nodding off. It was his first day back at school since his
		grandmother died. The sleepless nights had been piling up, and Donald was having trouble
		focusing.
	</p>
	<p class="mb-4">
		"The Germans moved their troops at night," Mr. Todd said. He ran his hand through his gray hair
		and became more animated as he spoke. The syllabus showed that they should be studying the
		Renaissance movement, but WWII was Mr. Todd's favorite subject. "The Allied forces were
		completely unaware of the advancing Germans, and they were completely surprised by the attack!"
	</p>
	<p class="mb-4">
		Donald forced his eyes to stay open. He tried to focus his attention on Mr. Todd, who was waving
		his arms in the air and pacing across the front of the room. The words washed over Donald like a
		warm bath, and he could feel himself drifting back to sleep. His breathing slowed, his eyelids
		grew heavy, and his head nodded forward.
	</p>
	<p class="mb-4">
		Donald jerked himself upright and opened his eyes. He was in a classroom, but it was not his
		classroom. A brick wall was the only view through the windows, but it let in enough pale light
		to illuminate the destroyed room. All the desks, including the one in which he sat, were old and
		rotting.
	</p>
	<p class="mb-4">
		It looked like the building had burned long ago, and years of water damage and mildew slowly
		rotted the once pristine furniture. The warped chalkboard at the front of the classroom still
		showed remnants of some long ago lesson, in a language that Donald could not read. He thought it
		might be German. An upside-down crucifix hung on the wall above the board.
	</p>
	<p class="mb-4">
		"No, no, no," Donald whispered. His body started shaking, and he grabbed the sides of his
		desktop to steady himself. The rotten damp wood squished in his grip and fell apart. He jumped
		out of the seat and rubbed his hands on his pants to clean off the slime. It left black streaks
		on the front of his jeans.
	</p>
	<p class="mb-4">
		He started breathing quickly, and he could feel the tears welling up in his eyes. "Please," he
		said to the empty room, "I just want to go home." He slid out of the seat and onto the floor.
		"Nana, I need you," he said. There was no answer. Donald curled up into a ball and cried.
	</p>
	<p class="mb-4">
		His stomach eventually growled. He had lost track of time, but it felt like he had been in the
		burnt room for hours. Exhaustion had replaced his fear, but he was worried that he was still in
		this place. He had hoped that he would just go back to his world all on his own.
	</p>
	<p class="mb-4">
		Light still came in through the windows, but it was not as bright as it had been before. Donald
		needed to figure out how to return home, and he wanted to be out of this place before it got
		dark. He mustered up his strength, and he left the room.
	</p>
	<p class="mb-4">
		Piles of moldy debris littered the hallway. Each pile was a child-sized lump of brownish-gray
		goo. They looked wet, and Donald did not want to know what would happen if he stepped on one. He
		carefully picked his way around the piles, only stepping on clear spots on the tile floor.
	</p>
	<p class="mb-4">
		Long scorch marks marred the walls, and they looked strange to Donald. Each one was a groove dug
		into the wall, with burn marks extending out along the wall from the center. It did not look
		like what he expected fire damage to look like. They reminded Donald of scratches made by claws.
	</p>
	<p class="mb-4">
		He came to a spot where he could not easily step onto another tile due to a cluster of moldy
		piles blocking his path. Donald spotted a clearing on the other side. He crouched down and
		leapt, but he landed short. One of his feet squished down into the middle of a pile. Something
		inside the pile crunched, and then it screamed.
	</p>
	<p class="mb-4">
		Something wrapped around his foot, and Donald yanked his foot back. A small child's hand held
		onto his ankle. It was pulling him, trying to get his foot back under the moldy surface. Donald
		kicked free and screamed.
	</p>
	<p class="mb-4">
		"Why did you hurt me?" said a voice. It was coming from inside the pile of mold. "Come play with
		me." The glob of mush quivered, and it slid an inch across the floor in Donald's direction.
	</p>
	<p class="mb-4">
		Donald was on his feet and running. He leapt over pile after pile, terrified to touch another
		one. Every one of them was moving now, and they called out to Donald as he ran by. The things
		moved in slow, jerky motions, creeping along the floor like deformed slugs. Donald had a vision
		of himself lying on the floor and the things slowly crawling on top of him where they would
		force open his mouth and crawl inside. He ran faster.
	</p>
	<p class="mb-4">
		Double doors at the end of the hallway were open to the outside. Donald sprinted down the last
		section of the hallway and out through the doors. He came out into a small courtyard paved in
		concrete. The sky was overcast, but it was much brighter outside than it had been in the
		building.
	</p>
	<p class="mb-4">
		Donald looked back. The piles of mold crept up near the door, but they stopped short of coming
		into the light. He could hear the childlike voices calling out, "Come play with us." Donald
		hunched over and vomited the bile from his empty stomach.
	</p>
	<p class="mb-4">
		He wiped his mouth on the back of his sleeve and stumbled away from the building, the voices
		fading away behind him. The brick walls of the school building twisted and turned, leading him
		on a winding path away from the entrance. Just as he was wondering if he had walked into a maze,
		the path opened up in front of him.
	</p>
	<p class="mb-4">
		He was on top of a small barren hill, with the remnants of a city stretching out below him.
		Shells of bombed out buildings covered the landscape, with a few still intact buildings
		sprinkled in between. Giant tentacled shapes moved amongst the streets of the ruined city. They
		were creatures with a form that he could not recognize, but a primal part of Donald's mind knew
		they were dangerous.
	</p>
	<p class="mb-4">
		Something scraped along the asphalt to Donald's right. He looked in that direction, and saw a
		rusted out car nestled against the side of the school building. The car door screeched open, and
		a person crawled out onto the ground. It could have been an old woman wearing a dress, except
		that its bald head was barely more than a skeleton. Thin gray skin stretched tight over the
		skull.
	</p>
	<p class="mb-4">
		The woman looked up at him and smiled. She had no eyes and no teeth. Her nose had sunken into
		her face. She looked back at the ground and crawled faster toward Donald. He could hear her bony
		hands scraping against the asphalt. "Get away!" Donald yelled, but she just crawled faster.
	</p>
	<p class="mb-4">
		She was getting closer. Donald turned to run, but his foot caught the lip of a pothole. He
		tripped and fell hard onto his side, knocking the wind out of his lungs. Stars danced in his
		vision, and he could not move. As the stars danced in front of his eyes, he screamed silently to
		himself. Run. I have to run!
	</p>
	<p class="mb-4">Donald felt a hand close around his ankle.</p>
	<p class="mb-4">
		He woke up in the school nurse's office. He tried to sit up, and the paper liner covering the
		bed crinkled under him. The nurse looked up from her desk, where she was filling out paperwork.
		"Donald," she said. "You gave us a fright."
	</p>
