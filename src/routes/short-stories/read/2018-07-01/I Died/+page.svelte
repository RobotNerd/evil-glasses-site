<span class="w-2/3 max-w-md">
	<h1 class="h2 mb-1 sm:h1 sm:mb-4">I Died</h1>
	<h3 class="h4 mb-8 sm:h3">by Sara Lufrano</h3>

	<p class="mb-4">
		I'm dead, he isn't. I know how it happened and everything, a cycling accident. A FedUP truck hit
		me.
	</p>
	<p class="mb-4">
		I was mostly in my bike lane and bam, splattered all over the truck and the road.
	</p>
	<p class="mb-4">
		My mom, dad, and little sister got paid handsomely from the settlement but he wasn't included
		since he was never legally family. He actually didn't even make it to any hearings.
	</p>
	<p class="mb-4">I know that because I've been with him, watching him, since it happened.</p>
	<p class="mb-4">
		I know it's sad when I look at him but I can't feel anything anymore. I see him cry, I see him
		lay in bed for days, I see him not eat, not answer his phone. I see him doing nothing and I know
		it's sad but there are no feelings in me.
	</p>
	<p class="mb-4">
		We were each other's soul mates. We jokingly talked about how we wouldn't be able to move on if
		the other died. Super funny.
	</p>
	<p class="mb-4">
		I had no idea I'd be watching him like this after and it makes me wonder who was watching me
		until I kicked it.
	</p>
	<p class="mb-4">
		He goes to a job, he lost his real one after it happened. He lives in an unfinished basement of
		someone's house after losing our apartment. And he sits there, or lays there, and does nothing
		until it's time to get up and get on the bus to his job.
	</p>
	<p class="mb-4">
		I'm not able to get his attention or move anything to show him I'm there, I can't talk to him
		during his dreams, or flicker lights when he cries out my name.
	</p>
	<p class="mb-4">
		So I wrap my arms around him and lay there with him. I always wish that this didn't happen but I
		feel no pain.
	</p>
	<p class="mb-4">Years passed and I'm still here with him.</p>
	<p class="mb-4">
		He was true to his word about not being able to move on. He doesn't cry anymore but he's still
		by himself in this basement working jobs, doing nothing but watching Netflix.
	</p>
	<p class="mb-4">
		He's thicker than he was a year ago but still not back to the size he was before. His hair and
		beard are long but he combs them and keeps them tidy. He got some new clothes that fit him
		better on his new frame.
	</p>
	<p class="mb-4">He's handsome now just like he always was.</p>
	<p class="mb-4">
		I don't know if anything is ever going to be different for me. I might watch him for his whole
		life and then when he dies maybe we'll be together. Maybe that thing will happen that when I do
		something good I go on to the true afterlife. Maybe I'll wander here forever even after everyone
		else is gone.
	</p>
	<p class="mb-4">
		I know that I don't want to leave him. Even though I'm dead and can't feel anything I know I
		don't want to be anywhere except with him.
	</p>
	<p class="mb-4">
		I've got my routine too. When he wakes up I give him a kiss and say “good morning, how did you
		sleep?” We sit together at his little outside bistro set that he has inside and have coffee. He
		gets ready, I give him his space then, and I sit and think or reread any text I can see.
	</p>
	<p class="mb-4">
		I go to work with him. He's started riding a bike again. I understood what he was going through
		everytime he looked at a it. I'm glad because I really did enjoy it, now I just know it as a
		reasonable mode of transportation.
	</p>
	<p class="mb-4">
		In the evenings and his days off we hang out and watch TV or he plays games on his console and
		then we go to bed.
	</p>
	<p class="mb-4">
		Recently he's been in better and better moods. He gets texts more often now and he's got more
		friends at his job. It's good for him. He smiles too.
	</p>
	<p class="mb-4">
		I do like seeing him this way. It's a small glimpse at how he was when I was alive.
	</p>
	<p class="mb-4">
		It had been months and he's grown further away from me. There's a woman at his job that he talks
		with, that talks with him. He smiles and laughs when she's around. They like each other.
	</p>
	<p class="mb-4">
		What she doesn't know is when he's home he cries because I'm gone. I think about what I would
		feel like if I could and it's satisfaction. But I only sit with him as he cries and doesn't
		respond to her texts, gently running my hand down his arm telling him everything will be okay.
	</p>
	<p class="mb-4">
		It's a few weeks later. He's been looking at his phone a lot but no one is texting or calling.
	</p>
	<p class="mb-4">
		The TV is off, it's sprinkling rain. He opens the door and steps outside. I follow him and stand
		next to him. He gets a shiver, I watch him shake it off.
	</p>
	<p class="mb-4">“Leave me alone,” he says staring out in the rain.</p>
	<p class="mb-4">
		I look to where he's looking but there's only the street, there's not even a house across the
		way.
	</p>
	<p class="mb-4">“I don't want to keep doing this.”</p>
	<p class="mb-4">He closes his eyes. He's crying.</p>
	<p class="mb-4">“If I say out loud that I don't love you anymore will you leave me alone?”</p>
	<p class="mb-4">Don't love me anymore? Who is he talking about?</p>
	<p class="mb-4">His hands are fists, down by his side. “I don't love you anymore!”</p>
	<p class="mb-4">
		I put my hand out to grab his shoulder and it goes right through him. I try again.
	</p>
	<p class="mb-4">“I want to forget you!”</p>
	<p class="mb-4">
		And then he was gone. Or I was gone. Either way we weren't together anymore. I know that in this
		moment I was happy that I didn't feel anymore.
	</p>
</span>
