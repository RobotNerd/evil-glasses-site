	<h1 class="h2 mb-1 sm:h1 sm:mb-4">Fishing Trip</h1>
	<h3 class="h4 mb-8 sm:h3">by Marshall Bowles</h3>

	<p class="mb-4">
		"Dad is cheating on Mom," Amanda said in a whisper. The sun was not up yet. She leaned out
		through the front door of the house, shivering in the cold morning air. She held her jacket
		closed with one hand.
	</p>
	<p class="mb-4">
		"What?" John said. He had a late flight the night before, and everyone was asleep by the time he
		got in. First time seeing his sister in a year, and she drops a bomb on him. John glanced over
		his shoulder. Their father was busy loading fishing gear into the truck. John looked back at
		Amanda and spoke softly. "That's crazy. He has the personality of a rock."
	</p>
	<p class="mb-4">
		"I didn't believe it either," Amanda said. She pulled out her phone and shoved the screen in
		John's face. It showed a picture of the computer in their father's home office, the one place in
		the house that was supposed to be off-limits to the rest of the family. A webpage was loaded up
		with a dating profile. John touched the screen to zoom in. It was definitely his father.
	</p>
	<p class="mb-4">
		"I found it yesterday while I was cleaning up," Amanda said. She had taken the whole week off to
		come home. John thought she was crazy for spending any more time here than necessary. "His
		computer wasn't even locked."
	</p>
	<p class="mb-4">
		“Why did you go in there?" John said, the anger in his voice surprising him. Amanda frowned, and
		John looked away. He took a deep breath. “Sorry. Have you said anything to him?"
	</p>
	<p class="mb-4">
		"No, you're the only person I've told, and I wish I didn't know." Her voice was shaky. Amanda
		slid her phone back into her pocket.
	</p>
	<p class="mb-4">John shook his head. "Ah, fuck."</p>
	<p class="mb-4">Amanda shrugged. "Happy Thanksgiving."</p>
	<p class="mb-4">
		"Ho!" Dad yelled from across the yard. John looked back at the truck. Everything was loaded and
		his father was waiting behind the wheel.
	</p>
	<p class="mb-4">"Maybe you can talk to him about it while you two are fishing," Amanda said.</p>
	<p class="mb-4">"No," John said. His stomach felt uneasy.</p>
	<p class="mb-4">
		"Or we'll just sweep it under the rug like everything else in this family," she said.
	</p>
	<p class="mb-4">
		"We're both adults now, and so are they," John said. He realized he was whisper-yelling. Amanda
		backed away a half-step. "I won't get involved in their lives more than I have to." He joined
		his father at the truck.
	</p>
	<p class="mb-4">
		The ride to the lake was long and familiar. John was twelve years old when they took the first
		Thanksgiving morning trek. John had not wanted to go, and he complained about it to his mother.
		“Can't I just stay home and let Dad go by himself?”
	</p>
	<p class="mb-4">
		“No, you'll go with your father and you'll like it,” Mom said. She used her end-of-discussion
		tone of voice, and John knew he would get in trouble if he pressed further. He wanted to tell
		her that he was afraid of Dad, but instead he ran to his bedroom and cried into his pillow.
	</p>
	<p class="mb-4">
		John and his father made the same trip every year since. It was their only father-son activity,
		which was one more than John wanted. Dad was like a giant pit bull that never barked and was
		always on the verge of biting. It was manageable when someone else was around, but John was lost
		when it was just him and Dad. Growing up and moving out of the house did not make the feeling go
		away.
	</p>
	<p class="mb-4">
		John looked out of the window at the dark shapes of trees and houses passing by. He breathed
		slowly and forced himself to stop gritting his teeth.
	</p>
	<p class="mb-4">
		The parking lot at the boat landing was empty. No one ventured out early on Thanksgiving morning
		to fish. Long ago John asked his father why he chose this day, and his father said, "Gets us out
		of the way so they can do the women's work."
	</p>
	<p class="mb-4">
		The air at the lake was cold and still, the surface of the water smooth as glass. John and his
		father sat in the small boat anchored close to shore. Fishing line stretched from John's pole
		down to the cork that floated in the water. The fish were not biting.
	</p>
	<p class="mb-4">
		Time dragged. John stopped bringing a watch after the first few years, because he would check it
		constantly and that only made the trip seem longer. The silence was too much to bear. "Looks
		like you finally got a new roof put on the house," John said, searching for anything that might
		make him feel less awkward.
	</p>
	<p class="mb-4">
		"Yep," Dad said, reeling in his line. He held his pole up in the air and checked the hook. No
		worm. He grunted and pulled a new one out of the plastic tub. He threaded the squirming worm
		onto the hook, cast his line out again, but said nothing more.
	</p>
	<p class="mb-4">
		An hour passed. Maybe two. Or maybe only ten minutes. John tried again. "I got a promotion at
		work," he said. He waited, but Dad did not respond. "I'm a manager now. They gave me a decent
		pay bump."
	</p>
	<p class="mb-4">"Okay," Dad said.</p>
	<p class="mb-4">
		Goddamn. Why did John even bother? It was like talking to a brick wall. The image on Amanda's
		phone popped into his head, but he tried not to think about it. He told himself not to get
		involved. He knew his parents were not happy together—they had never been—but they tried to hide
		it and live the lie.
	</p>
	<p class="mb-4">
		John reeled in his line and checked the hook. The worm was still there. He cast it out and it
		splashed into the water.
	</p>
	<p class="mb-4">This is the last year, John thought.</p>
	<p class="mb-4">
		John did not try to start up conversation again, and neither one of them caught anything that
		morning. Dad turned on the motor once the sun finished burning off the light morning mist. The
		boat cut through the water on its way back to shore and sent ripples out across the calm
		surface.
	</p>
	<p class="mb-4">
		Back at the house, the family of four sat down for the Thanksgiving meal. Mom and Amanda made
		light conversation, but John and Dad were quiet. The sound of the football game on the living
		room TV gave the room a false sense of energy. John picked at the food on his plate without much
		enthusiasm.
	</p>
	<p class="mb-4">"John, are you seeing anyone?" Mom said.</p>
	<p class="mb-4">
		It took him a moment to respond. He was fantasizing about an alternate reality where Mom
		divorced Dad. She remarried a TV weatherman who took John to baseball games and talked about how
		proud he was to be a stepfather. The sound of his name jarred him back to reality. "No, still
		single," he said.
	</p>
	<p class="mb-4">"Hurry up," she said. "I want grandkids. You're getting up in age."</p>
	<p class="mb-4">
		"I'm only twenty-seven," he said. He wanted to say, And what if I turn out like you and Dad?
	</p>
	<p class="mb-4">
		"<i>I'm</i> getting old," Mom said. "I want you to have children while I'm still around to see them.
		You need to set an example for your sister to follow." Amanda rolled her eyes and chewed on a mouthful
		of turkey.
	</p>
	<p class="mb-4">
		Mom reached out and put her hand on top of Dad's. Dad did not move his hand away, but the look
		on his face made John think he wanted to. Mom smiled. "I'm so blessed to have your father and
		the two of you. I want both of you to build happy families of your own."
	</p>
	<p class="mb-4">
		John gripped his fork so hard it hurt. What about all the one-sided arguments where you berated
		Dad in front of us? What about the time you told us that you would be happier if you killed
		yourself? And Dad always there, showing no reaction, the gravity of an emotional black hole in
		their lives. He looked to Amanda, but she avoided his gaze. She stared at a lump of cranberry
		sauce like it was about to jump off of her plate.
	</p>
	<p class="mb-4">John ate a forkful of mashed potatoes.</p>
	<p class="mb-4">
		The weeks rolled by, and John tried to forget Thanksgiving. He hated his family. His mother was
		too much of a coward to leave his father. Part of him felt bad for her, but only a small part.
		No part of him felt bad for his father.
	</p>
	<p class="mb-4">
		John laid awake in his bed on Christmas Eve. Unlike other families, his did not celebrate
		Christmas. He told himself he should be thankful that he only had to spend one holiday per year
		with them, but the taste of loneliness was bitter. Every time he closed his eyes to try to
		sleep, he saw the image on Amanda's phone.
	</p>
	<p class="mb-4">
		Tired of retracing the same memorized patterns on the ceiling, John got out of bed. He dug under
		a pile of dirty clothes and found his laptop. Screw it, he thought. If Dad was going to cheat,
		John would let him. Hell, he would even help.
	</p>
	<p class="mb-4">
		John logged into the dating site and built a fake profile for Sadie, an early-fifties widow who
		never had kids. He struggled to craft her into a woman he hoped would appeal to his father. The
		only album Dad ever owned was Queen's
		<i>A Day at the Races</i>, so Sadie's favorite bands were Queen and Pink Floyd. John used an
		image of a random woman who looked like a poor-man's Farrah Fawcett. Dad had commented on
		Farrah's body once while watching a rerun of Charlie's Angels.
	</p>
	<p class="mb-4">
		The trap was as ready as it could be. John found his father's profile on the dating site, and
		the status showed that Dad had been on the day before. John typed a simple message.
	</p>
	<br />
	<p class="mb-4">Hi, you seem nice. I'm Sadie.</p>
	<br />
	<p class="mb-4">He hit "Send." The line was cast and John waited for a bite.</p>
	<p class="mb-4">He awoke to a message the next morning. It was his father.</p>
	<br />
	<p class="mb-4">Nice to meet you, Sadie. I'm Chuck.</p>
	<br />
	<p class="mb-4">
		I read your profile and noticed that you're into 70's power ballads. Me too! My favorite band...
	</p>
	<br />
	<p class="mb-4">
		John raced through the message, unable to believe his eyes. There was more emotion in a few
		lines of text than John received from an entire lifetime with his father. By the last line,
		John's jaw was clenched so hard his teeth hurt.
	</p>
	<p class="mb-4">
		John looked up from the screen and stared out the window. The streetlight illuminated the cars
		parked along the sidewalk in a ghostly yellow light. It was hard to wrap his mind around what
		was happening. To know that his Dad had an actual personality hidden away, that he might be
		capable of caring about something—anything—made the lack of an emotional connection with his
		father feel worse.
	</p>
	<p class="mb-4">
		Screw you, Dad, John thought. He was going to set his Dad up and reveal everything when the time
		was right. John smiled at the thought of what his father's face would look like once he realized
		he was caught.
	</p>
	<p class="mb-4">
		The scam was easier than John expected. Dad was very open in his messages to Sadie. John kept
		everything light-hearted at first, feeling out how his father communicated. John had to guess at
		what his father might want in a woman, but it was tough work since he really knew nothing about
		the man.
	</p>
	<br />
	<p class="mb-4">
		<strong>Sadie</strong>: Top item on my bucket list? You may think this is silly, but I'd love to
		visit Mt. Rushmore. I can't believe I've never been...
	</p>
	<br />
	<p class="mb-4">
		<strong>Chuck</strong>: Bob Dylan or the Beatles? That's not fair. Only a monster would make a
		man choose a favorite...
	</p>
	<br />
	<p class="mb-4">
		<strong>Sadie</strong>: I'm too young to remember the Cuban Missile Crisis. But one of my
		earliest and favorite memories is when Neil Armstrong landed on the moon. I was sitting on the
		living room floor in front of the TV when...
	</p>
	<br />
	<p class="mb-4">
		<strong>Chuck</strong>: No, I never did anything that hard. Cocaine scared me. Besides I was out
		of my wild phase by the time it was popular. But who didn't get baked every once in a...
	</p>
	<br />
	<p class="mb-4">
		Enough time had passed, John felt confident enough to set the hook. He sat on the edge of his
		bed in his boxers, the laptop perched on his knees.
	</p>
	<br />
	<p class="mb-4">
		<strong>Sadie</strong>: So, I know this is a heavy question. Feel free not to answer if you
		aren't up to it. Any regrets in life?
	</p>
	<br />
	<p class="mb-4">
		John sent the message early on a Friday morning. Up until this point, his father's responses had
		been prompt. Nothing came later that day, and Saturday was the same. John worried that he had
		taken it too far. He was on the couch thinking about how to word an apology when a notification
		popped up on his screen. He opened it.
	</p>
	<br />
	<p class="mb-4">
		<strong>Chuck</strong>: Sorry for the delayed response, Sadie. You're right, that question is
		heavy. I've been thinking hard about it since you sent it.
	</p>
	<br />
	<p class="mb-4">
		I want to be up front with you. I'm married, and I'm still with her. Now that you know, I'll
		understand if you don't want to continue talking to me. That's ok, and I completely understand.
	</p>
	<br />
	<p class="mb-4">
		To answer the question: Yes, I have regrets. My biggest regret is marrying my wife. It's a long
		story, and...
	</p>
	<br />
	<p class="mb-4">
		That's it, John thought. Keep it coming Dad. Once Mom sees this, it will finally bring an end to
		this sham of a family. Maybe his father would finally show some emotion in real life.
	</p>
	<p class="mb-4">
		Satisfied with how smart he was, John looked back at the screen and read the rest of the
		message.
	</p>
	<br />
	<p class="mb-4">
		I don't want to bore you with the details. Let's just say that my marriage has been unhappy
		since the very beginning. My wife and I were dating at the time, and her pregnancy was
		unexpected. I married her the same week we found out because I thought it was the right thing to
		do.
	</p>
	<br />
	<p class="mb-4">
		Needless to say, it didn't work out well. We are two incompatible people who ended up stuck
		together. We had a second child and I hoped it would make us get along better. It did for a
		little while, but didn't last.
	</p>
	<br />
	<p class="mb-4">
		The first time I asked her for a divorce was when the kids were still in elementary school. I
		thought she would go for it because of how awful we were together. But she wouldn't agree to a
		divorce. She threatened to tell people I was abusive and swore that she would never let me see
		my children again.
	</p>
	<br />
	<p class="mb-4">
		So I stayed. My father walked out on me when I was young, and I hated him for it. I wasn't going
		to let my kids grow up without a father.
	</p>
	<br />
	<p class="mb-4">Anyway, that's that story.</p>
	<br />
	<p class="mb-4">
		John stared at the screen. What was this? He went through the message again to make sure he did
		not misread it. A pressure built up inside him, but John pushed it down. He slammed his laptop
		shut and threw it on the couch beside him.
	</p>
	<p class="mb-4">
		“No, Dad,” John said to the empty room. He paced in the small strip between the TV and the
		couch. “You don't get to do this. You don't
		<i>have</i> feelings.” He tried to stop it, but sympathy for his father was creeping in. His chest
		tightened and it was hard to breathe. For a moment, he felt like he was trapped back on the boat
		with his father.
	</p>
	<p class="mb-4">
		John shook his head. He was reading the situation wrong. He had to be. John grabbed his laptop
		and sent another message.
	</p>
	<br />
	<p class="mb-4"><strong>Sadie</strong>: So are you planning to leave her?</p>
	<br />
	<p class="mb-4">
		<strong>Chuck</strong>: No, I don't think so. I'm too old to start over.
	</p>
	<br />
	<p class="mb-4"><strong>Sadie</strong>: Then why are you on this site?</p>
	<br />
	<p class="mb-4">
		<strong>Chuck</strong>: I'm not sure. Maybe just trying to find someone who I can talk to. Is
		that bad?
	</p>
	<br />
	<p class="mb-4">
		He looked up from the screen and stared at his empty apartment. There were no pictures on the
		walls. The furniture was plain and cheap. For the first time, John saw it for what it was—a
		lonely, lifeless space.
	</p>
	<p class="mb-4">He sent another message.</p>
	<br />
	<p class="mb-4"><strong>Sadie</strong>: That's not bad at all.</p>
	<br />
	<p class="mb-4">
		The conversation between John and his father changed after that. John looked forward to their
		correspondence, because he was getting to know his Dad as a person. They spoke several times a
		week, sometimes multiple times per day. John gifted his own personality and experiences to
		Sadie.
	</p>
	<br />
	<p class="mb-4">
		<strong>Sadie</strong>: What's your favorite movie? Mine is Midnight in the Garden of Good and
		Evil.
	</p>
	<br />
	<p class="mb-4"><strong>Chuck</strong>: Road House. Swayze is my idol.</p>
	<br />
	<p class="mb-4">John rented Road House and watched it that night.</p>
	<br />
	<p class="mb-4">
		<strong>Chuck</strong>: How was your day? I threw my back out while I was cleaning the garage.
		It's times like these that make me realize I'm old.
	</p>
	<br />
	<p class="mb-4">
		<strong>Sadie</strong>: I did some spring cleaning myself, and I bought a painting to fill in an
		empty wall.
	</p>
	<br />
	<p class="mb-4">
		John had been at a garage sale earlier that day, where he found a painting of a small sailboat
		in rough seas. He hung it above the couch.
	</p>
	<br />
	<p class="mb-4">
		<strong>Sadie</strong>: My boss chewed me out again, but I'm getting used to it. I'm looking
		forward to the day I can retire.
	</p>
	<br />
	<p class="mb-4">
		<strong>Chuck</strong>: Don't let it get you down. Bosses only act like assholes because they
		can.
	</p>
	<br />
	<p class="mb-4">That made John smile. His boss <i>was</i> an asshole.</p>
	<p class="mb-4">On a morning in mid-September, John woke up to a new message.</p>
	<br />
	<p class="mb-4">
		<strong>Chuck</strong>: Out of left field, I know, but what's your darkest secret?
	</p>
	<br />
	<p class="mb-4">John was practiced at being Sadie, and he had no trouble responding.</p>
	<br />
	<p class="mb-4">
		<strong>Sadie</strong>: I've never told this to anyone else. I hated my father my whole life.
		But now that I'm older, I feel like he and I have reconciled. Or, at the very least, I
		understand him better.
	</p>
	<br />
	<p class="mb-4">What about you? Darkest secret?</p>
	<br />
	<p class="mb-4">Dad's message came back later the same day.</p>
	<br />
	<p class="mb-4">
		<strong>Chuck</strong>: When I found out my wife was pregnant with our son, I asked her to have
		an abortion.
	</p>
	<br />
	<p class="mb-4">
		John closed his laptop and set it on the floor. He felt like he had been punched in the gut, the
		pain radiating outward to consume his whole body. What an idiot he had been. How could he have
		allowed himself, even for a second, to care about his father?
	</p>
	<p class="mb-4">
		The scam was over. John did not care about exposing his father anymore. He did not care about
		anything. What was the point? His family was toxic, and the only reasonable thing to do was to
		cut them out of his life.
	</p>
	<p class="mb-4">
		The next day was a blur. John's thoughts kept drifting back to his father's last message, no
		matter how much he tried to avoid it. He always knew that his father did not want him, but some
		part of him hoped he was wrong. Getting proof hurt. John wanted to be angry about it, but he
		just felt empty inside.
	</p>
	<p class="mb-4">
		That night, a notification popped up on his screen. It was a new message from his father. Don't
		read it, John thought. Just delete the account and be done with it. Still, he opened the message
		and read it.
	</p>
	<br />
	<p class="mb-4">
		<strong>Chuck</strong>: Sadie, sorry about that last message. I was having a rough day and was
		in a dark place. I hope I didn't offend you. I worry that I did since you haven't responded.
	</p>
	<br />
	<p class="mb-4">
		What I wrote yesterday is a burden that I've lived with for the past twenty-seven years. There
		isn't a day that goes by where I don't feel the guilt of it weighing me down. Every time I look
		at my son, I think of how he would have never been alive if I had gotten what I asked for.
	</p>
	<br />
	<p class="mb-4">
		Having him changed me, and it's what made me happy to have my daughter a few years later. The
		best thing about my son is how he has grown up to be a better man than I ever was. I'm so proud
		of him sometimes it hurts.
	</p>
	<br />
	<p class="mb-4">
		John read the last line again. His heart pounded and he felt dizzy. Dad was proud of him. That
		was the only thing John had ever really wanted. A tear rolled down his cheek and dripped onto
		his keyboard. He used the end of his shirt sleeve to wipe it off.
	</p>
	<p class="mb-4">John typed one last message to his father.</p>
	<br />
	<p class="mb-4">
		<strong>Sadie</strong>: It's okay. You didn't offend me, Chuck.
	</p>
	<br />
	<p class="mb-4">
		Your last message made me think a lot about myself and where I am at this point in my life.
		Talking to you has helped me through a difficult time, but now I've realized that it isn't
		healthy for me to continue hiding like this online.
	</p>
	<br />
	<p class="mb-4">
		Our conversations have been wonderful and I will think back fondly on them. Take care, Chuck.
	</p>
	<br />
	<p class="mb-4">He deleted the account.</p>
	<p class="mb-4">
		John flew home a few weeks later for Thanksgiving. He woke up early in the morning and quietly
		helped his father pack the fishing gear in the truck. They rode down the winding road to the
		lake, and got the boat out onto the water just as the sun came up. Like the year before, the
		fish were not biting.
	</p>
</span>
