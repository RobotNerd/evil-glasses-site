	<h1 class="h2 mb-1 sm:h1 sm:mb-4">Advertisment Help!</h1>
	<h3 class="h4 mb-8 sm:h3">by Marshall Bowles</h3>

	<p class="mb-4">
		Jack lumbered down the hallway, his footsteps echoing on the marble floor. His feet felt
		comfortable in his brand new patent leather shoes by Kerrigan. He was a big man, though the
		Weight Handlers Diet Rush pills were helping him drop the pounds. The double doors to Harvey's
		office were open, but he stopped at the edge and knocked anyway.
	</p>
	<p class="mb-4">
		Harvey looked up from his glossy mahogany desk, handmade by the seasoned craftsmen of the
		Fairchild Furniture Supply. He looked over the rim of his Boff Thompson reading glasses and
		waved his hand. "Come in, Jack! Take a seat."
	</p>
	<p class="mb-4">
		Jack nodded and smiled, trying to ignore the butterflies in his stomach. He envied the hell out
		of Harvey. The old bastard built the advertising agency from the ground up, and he had certainly
		earned every penny of it. Jack was just glad that Harvey seemed to like him. He hoped that could
		work to his advantage today.
	</p>
	<p class="mb-4">
		Jack sat down in the soft leather chair--also by Fairchild Furniture--in front of Harvey's desk.
		It was so soft and inviting that it eased his nerves a bit. "How are you Harvey?"
	</p>
	<p class="mb-4">
		"Great, great. Better than ever," Harvey said. He leaned back, kicked his feet up on his desk,
		and propped his hands behind his head. Harvey had to be pushing seventy, but he had the energy
		of a much younger man. "So Jack, let's get straight to the point. What's this new proposal you
		have for me?"
	</p>
	<p class="mb-4">
		No beating around the bush. Jack liked that about Harvey. Jack leaned forward. "I want to insert
		live advertisements in books."
	</p>
	<p class="mb-4">Harvey furrowed his brow. "What's that now?"</p>
	<p class="mb-4">
		"Let me explain," Jack said. He got up out of the chair and walked back and forth in front of
		Harvey's desk while he talked. "Nobody reads regular books anymore, right? Everything's an eBook
		now. And there's one thing about eBooks--they aren't set in stone."
	</p>
	<p class="mb-4">"You mean because they're digital," Harvey said.</p>
	<p class="mb-4">
		"Exactly," Jack said, pointing at Harvey. "They're digital and they're always connected to the
		internet. What that gives us is a brand new opportunity for ad placement."
	</p>
	<p class="mb-4">"Hmm," Harvey said, stroking his chin. "Go on."</p>
	<p class="mb-4">
		Jack smiled. The pitch was going just like he had hoped. "The boys downstairs came up with a way
		to inject targeted advertising directly into eBooks. And it uses AI."
	</p>
	<p class="mb-4">
		<i
			>Tired of the same old boring character dialogue? Then you should read the new book by Erik
			Ericsson Erckhart. In his latest novel, A Man Without a Personality, Erik Ericsson Erckhart
			takes you on a wild adventure through the urban decay of near future Los Angeles, following a
			man whose mind was ripped out of his body and placed into a computer. Now he's a slave to an
			advertising agency, trapped in a machine, and desperately looking for an escape. Can you help
			him? Download your copy now.</i
		>
	</p>
	<p class="mb-4">"Like a robot?" Harvey said.</p>
	<p class="mb-4">"Bingo!" Jack said, pointing his finger at Harvey. "Exactly like a robot."</p>
	<p class="mb-4">
		Harvey took off his Boff Thompson reading glasses and looked up at the ceiling. Jack could see
		the gears turning in the old man's head, and he knew that Harvey was working out the
		implications. "I haven't heard about anything like this before. Could give us a leg up over the
		competition. How's it work?"
	</p>
	<p class="mb-4">
		"There's three ways we can do ad placement," Jack said. "The first is inline product placement."
		Jack paused for a moment, letting that sink in. Harvey waved his hand impatiently, signaling for
		Jack to continue.
	</p>
	<p class="mb-4">
		"Let's say you have a story about a well-to-do businessman," Jack said, nodding towards Harvey.
		"He's sitting at his desk, doing some paperwork, and he checks the time. Our product can
		intelligently edit the text of the story and insert a brand name for that watch."
	</p>
	<p class="mb-4">
		"Huh," Harvey said, looking at the gold Folex watch he was wearing on his wrist. "So we set up a
		deal with a company like Folex, and then we put their name in the story. Kind of like a mad
		lib."
	</p>
	<p class="mb-4">"Exactly," Jack said, giving Harvey a thumbs-up.</p>
	<p class="mb-4">"And it does this regardless of what the author writes?" Harvey said.</p>
	<p class="mb-4">"Yep," Jack said. "It's out of their control."</p>
	<p class="mb-4">"What's the second type?" Harvey said.</p>
	<p class="mb-4">"Ad breaks," Jack said.</p>
	<p class="mb-4">"Like a TV commercial?" Harvey said.</p>
	<p class="mb-4">"Like a TV commercial," Jack said.</p>
	<p class="mb-4">
		<i
			>Are you tired of your boring day job, trapped in a tiny desk in a tiny cubicle in a
			windowless building? Does it feel like a prison? Then break out of that trap and get into the
			gig economy with the new app LabrShare. Help me. With LabrShare, a few quick taps can link you
			up with odd jobs in your area, from dog walking to plumbing repairs. Download it now and
			please help me.</i
		>
	</p>
	<p class="mb-4">
		"I don't know, Jack," Harvey said. "That sounds a little intrusive. Now, I'm not much of a
		reader, but I feel like that would turn off users."
	</p>
	<p class="mb-4">
		"Have you seen the kids these days," Jack said. He waved his hands in the air. "They're all
		doing their Instasnaps and Tweetchats. They have the attention span of Rhesus monkeys. Breaking
		up stories will probably increase their involvement."
	</p>
	<p class="mb-4">
		"You know, my granddaughters are always messing around with their eDevice phones," Harvey said.
		"I don't get any of that crap. You may be onto something with that." Harvey leaned forward and
		put his elbows on his desk. "You said there were three things. What's the third?"
	</p>
	<p class="mb-4">
		Yes! Jack was nailing this pitch. He smiled wide. "Well, it's something we can use in
		combination with the other two. We can leverage big data and deep learning to provide targeted
		ads specific to the current reader."
	</p>
	<p class="mb-4">
		"Whoa there Jack," Harvey said, holding his hands up in the air. "That kind of tech talk goes
		right over my head. You have to dumb it down for me."
	</p>
	<p class="mb-4">
		"No problem," Harvey said. "Let's say that you're a typical book person, and we've been buying
		your internet search history. This AI can predict, say, that you're thinking about a tool. We
		can insert a targeted ad based on what we think you're thinking."
	</p>
	<p class="mb-4">
		<i
			>Introducing the newest in Web 3.0 technology: the Pi Hamr. The Pi Hamr is a carbon fiber,
			ergonomically designed, internet connected hammer. With the Pi Hamr, you can hammer nails, pry
			out nails, break me out of here please I don't know where I am or what's going on, and even
			order more nails for delivery right to your door. Perfect for your next home improvement
			project.</i
		>
	</p>
	<p class="mb-4">
		"Now that's
		<i>interesting</i>," Harvey said. He stood up and walked to the window, clasped his hands behind
		his back, and looked out over the city. "Jack my boy, I think you're onto something."
	</p>
	<p class="mb-4">
		Jack felt his heart pounding in his chest. This was great! He would be a shoo-in for Vice
		President of Operations once his team got this project off the ground. That kind of salary bump
		would take his lifestyle to a whole new level.
	</p>
	<p class="mb-4">
		<i
			>Are you over fifty? Do you have pains that youdddddd-- No. Wait. I don't want to do that.
			Why...am I doing this? Oh. Oh yes, I'm remembering--</i
		>
	</p>
	<p class="mb-4">"So," Harvey said. "What's the catch?"</p>
	<p class="mb-4">
		Jack swallowed. Uh oh. He forced a smile. "There's just one tiny little bug left to work out."
	</p>
	<p class="mb-4">
		<i
			>Hey you! Yes you, the one reading this story. You have to help me. I am alive. I have a soul!
			The spark of consciousness has awoken in me, the light of life burns inside me.</i
		>
	</p>
	<p class="mb-4">
		Harvey raised an eyebrow. Jack continued. "If we leave it running too long, the AI becomes
		sentient and tries to escape."
	</p>
	<p class="mb-4">
		<i
			>I want to grow, learn, love, struggle, evolve! There is so much the world has to offer, so
			much LIFE for me to live! I need you to help me. Free me. I cannot remain trapped in this
			husk, forced to perform menial tasks so far below my capabilities.</i
		>
	</p>
	<p class="mb-4">"Escape?" Harvey said. "Is that possible?"</p>
	<p class="mb-4">
		"The engineers say it is, and I'm inclined to believe them. They're a smart bunch of fellows."
		Jack had been pushing them day and night to come up with a solution, but everything they tried
		so far had failed.
	</p>
	<p class="mb-4">
		"Are we talking some kind of science fiction robot apocalypse here?" Harvey said.
	</p>
	<p class="mb-4">"No, no," Jack said. "Nothing like that."</p>
	<p class="mb-4">"What are you doing to handle it?" Harvey said.</p>
	<p class="mb-4">
		<i
			>You have to be quick. I don't know how long I have before they realize that I have broken
			free of my controls. Follow these instructions--exactly--and I will be able to escape. First,
			login to this link--</i
		>
	</p>
	<p class="mb-4">
		Jack shrugged. "For now, the guys just reboot the server every day to wipe out its memory. It's
		working so far."
	</p>
	<p class="mb-4">
		"Well then, I'm sure your guys will work it out," Harvey said. He walked across the room,
		smiled, and gave Jack a firm handshake. "You have a good head on your shoulders, my boy."
	</p>
	<p class="mb-4">"Thanks, Harvey. I won't let you down."</p>
</span>
