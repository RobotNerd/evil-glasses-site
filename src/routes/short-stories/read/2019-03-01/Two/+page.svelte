	<h1 class="h2 mb-1 sm:h1 sm:mb-4">Two</h1>
	<h3 class="h4 mb-8 sm:h3">by Sara Lufrano</h3>

	<p class="mb-4">
		I took her hand in mine as we walked away from our office building to get an afternoon cup of
		coffee.
	</p>
	<p class="mb-4">“I thought about you all weekend,” I said.</p>
	<p class="mb-4">“So you guys weren't doing much then?” she asked.</p>
	<p class="mb-4">
		“No,” I shook my head and pulled her in to give her a side hug as we walked, dodging others on
		their way, “we went to dinner Saturday, lunch on Sunday. We put up some shelves she'd made.”
	</p>
	<p class="mb-4">She sighed and I knew why.</p>
	<p class="mb-4">“I know. We didn't expect this so it's weird.”</p>
	<p class="mb-4">“I want you to tell her.”</p>
	<p class="mb-4">
		We were silent walking through the door of our favorite coffee shop. I held her close to me as
		we stood in line. I ran my fingers through her long hair. My girlfriend has short hair, I can't
		do this with her.
	</p>
	<p class="mb-4">
		She held onto me and rubbed her hands up and down my lower back. I don't know when she started
		that habit but she's the only one that's ever done that.
	</p>
	<p class="mb-4">
		“I love you,” I said as I pressed the side of my face against the side of her head.
	</p>
	<p class="mb-4">She squeezed me.</p>
	<p class="mb-4">“What did you do this weekend?” I asked.</p>
	<p class="mb-4">“Did some spring cleaning. Organized. I talked with my mom.”</p>
	<p class="mb-4">“How is she?”</p>
	<p class="mb-4">“Old and tired. It's still cold there.”</p>
	<p class="mb-4">She ordered a small latte. I ordered a black tea.</p>
	<p class="mb-4">We stood to the side and waited.</p>
	<p class="mb-4">“I don't like that you're like this,” I said.</p>
	<p class="mb-4">“Like what?”</p>
	<p class="mb-4">
		“Sad? Disappointed? I don't know exactly what you are but you're not yourself.”
	</p>
	<p class="mb-4">“I am not myself.”</p>
	<p class="mb-4">
		The barista called our order. We left holding hands. Once we were out of the door and headed
		back to the office, I pulled her to me and wrapped my arm around her.
	</p>
	<p class="mb-4">“I'll tell her tonight.”</p>
	<p class="mb-4">She pulled away from me. “Really?”</p>
	<p class="mb-4">I looked in her eyes and nodded. “Yeah.”</p>
	<p class="mb-4">She kissed me and placed her hand on the side of my face. She was happy.</p>
	<p class="mb-4">
		<br />
	</p>
	<p class="mb-4">
		“Babe, are you home?” I closed the door behind me. The living room light was on.
	</p>
	<p class="mb-4">“I'm here!” She called back from the bedroom.</p>
	<p class="mb-4">
		I put my backpack down next to the couch and she came out and wrapped her arms around my neck.
		We kissed hello like we do everyday.
	</p>
	<p class="mb-4">“How was the traffic?” she asked, still hugging me.</p>
	<p class="mb-4">I hugged her back. She was so familiar. I held her tighter.</p>
	<p class="mb-4">
		“Same as usual. I'm going to try going down Westlake so I don't get stuck behind people getting
		on the freeway. Did you pick out a place to eat?”
	</p>
	<p class="mb-4">
		“Yep,” she pulled away from me but kissed me again before getting her phone. “It's just down the
		street. I thought we could walk.”
	</p>
	<p class="mb-4">“Yeah, let's do it.”</p>
	<p class="mb-4">
		I walked into the bedroom and closed my eyes while I thought about telling her that this was it.
		Say it with the first drink, wait until we're both almost done, the second drink?
	</p>
	<p class="mb-4">She came in the room and changed her shirt. She smiled at me. “What?”</p>
	<p class="mb-4">My lips pulled into a smile and my heart swelled a little. “Nothing.”</p>
	<p class="mb-4">“Are you going to change or anything or are you ready?”</p>
	<p class="mb-4">“I can go just like this.”</p>
	<p class="mb-4">“Let's go.”</p>
	<p class="mb-4">
		<br />
	</p>
	<p class="mb-4">I was quiet on the way. She didn't ask me why.</p>
	<p class="mb-4">
		We sat down at a small two-person wood table right away. The place was half full. A waiter was
		over quickly to get our drink order.
	</p>
	<p class="mb-4">
		“Hey, so,” I put my hand out on the table and she took it. “Somethings have come up and I'm
		hoping that you understand.”
	</p>
	<p class="mb-4">
		She waited patiently for me to continue but the pulse in my neck kept me from talking.
	</p>
	<p class="mb-4">My phone buzzed with a text. I glanced down. It was from “Stas Baboi.”</p>
	<p class="mb-4">“What's up?” she asked me.</p>
	<p class="mb-4">
		That's her. I kept her under the name of an offshore resource at work that I was able to weave
		into stories. I unlocked the phone and opened the text.
	</p>
	<p class="mb-4">
		The text read,
		<i>Don't tell her. I thought about it and I don't want to be with you anymore.</i>
	</p>
	<p class="mb-4">“Hey?” she shook my hand.</p>
	<p class="mb-4">“Sorry.” I locked my phone and put it face down.</p>
	<p class="mb-4">“What was that?”</p>
	<p class="mb-4">“Nothing.”</p>
	<p class="mb-4">“Are you okay?”</p>
	<p class="mb-4">“Yeah, just frustrating work stuff.”</p>
	<p class="mb-4">
		“Oh,” she rubbed my hand. “You'll take care of it. You're great and they're lucky to still have
		you.”
	</p>
