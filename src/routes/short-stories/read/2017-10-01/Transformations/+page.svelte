	<h1 class="h2 mb-1 sm:h1 sm:mb-4">Transformations</h1>
	<h3 class="h4 mb-8 sm:h3">by Marshall Bowles</h3>

	<p class="mb-4">
		Sorry if there are typos. It's hard to use a laptop now that my hands aren't human anymore. I'm
		recounting my story for any of you out there who don't remember the previous timeline. Maybe one
		of you knows how to fix things.
	</p>
	<p class="mb-4">
		Before I entered the time portal, the military scientists tried to explain the rules. I didn't
		get it. "You're sending me back to before I had my accident, and it will be like it never
		happened?"
	</p>
	<p class="mb-4">
		"Correct," Walker said, pushing his glasses up higher on his nose. "And, of course, the
		assassin's tool you carry was manufactured prior to the target point. This ensures that it will
		not disassemble into its precursors when making the trip."
	</p>
	<p class="mb-4">
		"What?" I looked down at the fake diamond ring on my hand, which Walker had given me a few
		minutes earlier.
	</p>
	<p class="mb-4">
		"It doesn't matter," Faraday said. She was fiddling with the knobs on the time portal's control
		panel.
	</p>
	<p class="mb-4">
		"It's really quite simple," Walker said in a lecturing tone. I could tell he wasn't
		intentionally trying to be obnoxious. He was just an obnoxious person. "Any energy-matter
		signature sent through the portal occupies its previous point in spacetime. However, these
		events have not yet been applied to your body's energy-matter signature at the target point in
		spacetime."
	</p>
	<p class="mb-4">
		Faraday noticed the <i>WTF</i> look on my face, and she butted in. "It doesn't have to make sense.
		That's just how it is. Accept it and focus on killing Arkin."
	</p>
	<p class="mb-4">
		But wait, I'm getting ahead of myself. I need to go back further. Back to the day of the
		accident that took my legs and put me in a wheelchair. Back to the day I met Arkin.
	</p>
	<p class="mb-4">
		The first class seat was worth the extra ninety dollars it cost me to upgrade. I hadn't slept
		well the previous week. Work sent me to Los Angeles for a conference, and I was never
		comfortable in hotel rooms. I hoped to sleep the whole flight back to New York.
	</p>
	<p class="mb-4">
		A man was sitting in the window seat next to mine when I boarded. His greasy hair was matted
		down against his head. Clearly he wasn't a fan of hygiene. "Hi," he said when I sat down. He
		showed a set of crooked teeth when he smiled.
	</p>
	<p class="mb-4">"Hi," I said. I avoided eye contact. No need to encourage an airplane talker.</p>
	<p class="mb-4">"I'm going to turn everyone into airplanes," he said.</p>
	<p class="mb-4">
		I sometimes wonder how things would have turned out if I had asked the flight attendant to move
		me to another seat. But I didn't. It was the weirdest thing I ever heard anyone say in my life,
		and I had to know more.
	</p>
	<p class="mb-4">
		His name was Arkin, and he claimed to be a scientist of some kind. "What kind of scientist can
		turn people into airplanes?" I asked. "A geneticist? Nuclear physicist?"
	</p>
	<p class="mb-4">"Something like that." I waited for more, but he didn't elaborate.</p>
	<p class="mb-4">
		"Ok, sure," I said, trying to keep a straight face. He probably printed out and framed a
		"science" diploma from a website. "So how do you go about turning people into airplanes?"
	</p>
	<p class="mb-4">
		"It's easier than you think." Arkin launched into a long explanation and said a lot of sciency
		stuff that I didn't understand. Retroviruses. Nanomachines. Reality distortion manifolds. "Right
		now I can only convert one person at a time. The hard part is figuring out how to do everyone at
		once."
	</p>
	<p class="mb-4">"But why airplanes? Why not animals, or furniture?"</p>
	<p class="mb-4">
		Arkin looked out the window at the clouds drifting by. He ran his finger down the inside of the
		plane, caressing the hard plastic. "Because planes are beautiful."
	</p>
	<p class="mb-4">"What if I don't want to be turned into an airplane?" I said.</p>
	<p class="mb-4">
		"Most people are like you," he said. "They only see how things are and not how things should
		be."
	</p>
	<p class="mb-4">"So you know what's best for everyone?" I said.</p>
	<p class="mb-4">"Yes," he said.</p>
	<p class="mb-4">I rolled my eyes. "I don't believe anything you're saying."</p>
	<p class="mb-4">
		Arkin nodded. "Of course not. But you will." He looked out the window for the rest of the
		flight.
	</p>
	<p class="mb-4">
		We landed and I decided to take a taxi instead of the train. While I waited in line for a cab, I
		wrote a Facebook post and recounted every detail of my conversation with Arkin. I even included
		a picture I surreptitiously took of him during the flight.
	</p>
	<p class="mb-4">
		On the ride home a truck rear-ended the cab at a stoplight. One moment I was checking my phone
		to see if anyone had liked my post yet, and the next my legs were crushed in a tangle of metal.
		The cab driver didn't even get a bruise. There was nothing the doctors could do. They amputated
		both of my legs at the knee.
	</p>
	<p class="mb-4">
		That was the end of my old life. I wish I could say I wasn't bitter. I wish I could say that I
		overcame the odds and went on to achieve great things in spite of my disability. I wish I hadn't
		lost my legs. But no matter how hard I wished, I knew that none of it would come true. So I
		stopped caring, got a job I hated that allowed me to work from home, and I checked out on life.
	</p>
	<p class="mb-4">
		A few years went by. One morning I was sipping my coffee when I opened my laptop and read the
		news. There had been reports about a new flu-like virus spreading for the last week, but no one
		knew much about it. I wasn't concerned—there were a few benefits of being a recluse. But the
		virus had taken a new turn overnight. The first headline summed it up:
		<i>Virus Turns People Into Airplanes</i>.
	</p>
	<p class="mb-4">
		My jaw hit the floor. It couldn't be. I opened the news website and watched a shaky handheld
		video of a middle-aged woman screaming and running into a field. She stumbled and fell to her
		hands and knees, and her skin started swelling outward like a balloon.
	</p>
	<p class="mb-4">
		What remained at the end of the transformation was an abomination. It was the size and shape of
		a passenger jet but composed of flesh. The fuselage was covered in skin, the ailerons were made
		of fingers, and the vertical tail was bone. The worst part were her eyes, stretched out in place
		of cockpit windows and leaking giant tears.
	</p>
	<p class="mb-4">
		There was a knock at my door. I opened it to find two serious looking men in yellow hazmat
		suits. One of them held up his phone so that the screen faced me. "Ma'am, did you write this
		Facebook post?"
	</p>
	<p class="mb-4">
		That's how I found myself in a giant military facility buried deep under a mountain in an
		undisclosed location. They stuck me in a bleak, gray interrogation room by myself. I could see
		my reflection in the one-way mirror, and I wondered if anyone watched me from the other side.
	</p>
	<p class="mb-4">
		After hours of waiting, the door opened and a woman wearing a white lab coat walked in. The name
		patch sewn onto her lab coat read Faraday. "Sorry to keep you waiting," Faraday said.
	</p>
	<p class="mb-4">
		"I don't know why you people hauled me off to this place, but I know you aren't stupid enough to
		believe that I had something to do with this virus because of a Facebook post—"
	</p>
	<p class="mb-4">
		Faraday held up her hand and cut me off. "We have a lot to talk about," she said. "I'll answer
		your questions while we go." She nodded her head toward the hallway. "After you."
	</p>
	<p class="mb-4">
		I thought about staying in place just to piss her off, but my curiosity was stronger than my
		anger. They didn't bring me here for fun. I had to know what the military wanted out of a
		disabled recluse. I wheeled myself out into the long, plain hallway. The walls were the same
		drab gray as everything else. "What am I doing here?" I said.
	</p>
	<p class="mb-4">
		"Three years ago, you sat on a flight beside a man named Ulrich Arkin." Faraday led me deeper
		into the facility. We passed plain metal doors, identical except for the room numbers on plates
		beside each one. "He told you he was going to turn people into airplanes."
	</p>
	<p class="mb-4">"Yeah, I thought he was crazy," I said. "I guess he wasn't."</p>
	<p class="mb-4">
		"Anyone who engineers a virus that turns people into airplanes is the definition of crazy,"
		Faraday said. She stopped at room 834R and swiped her ID badge. The door slid opened on its own,
		and I rolled myself through into a large, cavernous room. In the center, a glowing bluish sphere
		floated in the air. Its surface shimmered like water rushing in a stream.
	</p>
	<p class="mb-4">
		"What is that?" I stared at the glowing sphere, mesmerized. The pale light grew until it
		consumed my vision. My body became weightless and I floated away into space. I drifted past
		stars, black holes, entire galaxies, and ultimately into the pure void beyond existence itself.
		I was one with the infinite unknown.
	</p>
	<p class="mb-4">
		"It's a time portal," Faraday said. I snapped back to reality. She grabbed the handles of my
		wheelchair and pushed me to the edge of the room, parking me by a table. Another
		scientist—Walker—was there, holding a metal briefcase. "But we'll get to that in a minute.
		First, we need to talk about how you're going to kill Arkin."
	</p>
	<p class="mb-4">
		I'm not dense. I've seen plenty of science fiction movies. "No way. I'm not going back in time
		to be your assassin. Send somebody else."
	</p>
	<p class="mb-4">
		"There is no one else," Walker said. "You are the only available vector pinpointing the target
		at any known point in spacetime."
	</p>
	<p class="mb-4">"Huh?" I said.</p>
	<p class="mb-4">
		"There are limitations," Faraday said. "It's only possible for a person to travel back to a time
		and place they previously experienced, and they only go back for a period of twenty-four hours."
	</p>
	<p class="mb-4">
		"There must be a lot of other people who can do it. Someone else who knew Arkin," I said.
	</p>
	<p class="mb-4">
		Walker put the briefcase down on the table and opened it. Inside was a small jewelry box packed
		in foam, the kind meant to hold an engagement ring. "All of our other potentials have been
		infected or completed the transformation. You're the only one left."
	</p>
	<p class="mb-4">
		Faraday leaned over and put her hands on my shoulders. She looked into my eyes. "You're our only
		chance. If you don't succeed, the human species is done."
	</p>
	<p class="mb-4">
		I looked down at my lap. In that moment, I thought about how I had given up on life the day that
		I lost my legs. There was nothing I wanted, no goal to live for. I just existed. And now I
		finally had a chance to do something important. "Yes," I said.
	</p>
	<p class="mb-4">
		Walker nodded. He opened the jewelry box, pulled out a simple diamond ring, and held it out for
		me. I reached for it, but Walker pulled it back a hair. "Careful. It's full of poison."
	</p>
	<p class="mb-4">
		A half hour later, my wheelchair sat on the edge of the platform at the precipice of the portal.
		I wore the diamond ring on my finger, careful not to touch it and accidentally deploy the
		miniature needle on the bottom. Walker called the ring a "CIA Special."
	</p>
	<p class="mb-4">
		Walker and Faraday stood to either side of me. I looked up at Faraday. "So I'm going to do this
		and everything will reset back to normal. Like nothing ever happened?"
	</p>
	<p class="mb-4">
		Faraday put her hands on her hips. "I know you're thinking about changing your own past. You'll
		avoid the accident that took your legs and then you'll try to invest in stocks or something."
	</p>
	<p class="mb-4">She must be able to read minds. "I wasn't—"</p>
	<p class="mb-4">
		Faraday cut me off. "Go right ahead. We don't care, as long as you complete your mission first."
		I felt a surge of hope. If I did this right, would I be able to walk again? Before I could dwell
		on it, Faraday and Walker grabbed me under my shoulders and lifted me out of my wheelchair. "You
		ready?" Faraday said.
	</p>
	<p class="mb-4">I wasn't. "Yeah," I said, taking a deep breath.</p>
	<p class="mb-4">
		"We're counting on you," Faraday said. She and Walker tossed me forward into the abyss.
	</p>
	<p class="mb-4">"I'm going to turn everyone into airplanes," Arkin said.</p>
	<p class="mb-4">
		I was back on the same airplane. Back in the past. I couldn't believe it worked. I looked down,
		past my knees, at the old legs I used to have before they were amputated. I lifted up my right
		one, then my left, then I kicked them back and forth, laughing like a kid.
	</p>
	<p class="mb-4">
		"They're back!" I yelled. I jumped up and danced in the aisle. I forgot how fantastic of a
		feeling it was to be on real legs. I started doing jumping jacks.
	</p>
	<p class="mb-4">
		"Ma'am, please take your seat," the flight attendant said. She gave me the evil eye. "People are
		trying to board."
	</p>
	<p class="mb-4">
		I plopped down in my seat, grinning ear to ear. "Hmm," Arkin said. He looked at me like I was
		the weirdo. What an unusual feeling, having a monster think you're strange.
	</p>
	<p class="mb-4">
		I remembered why I was there. The diamond on top of the ring shifted when I pressed it, causing
		the tiny needle to extend out from the bottom part of the band. I leaned over, grabbed the back
		of Arkin's hand, and I whispered, "You aren't going to turn anybody into anything."
	</p>
	<p class="mb-4">
		It bothered me that I didn't hesitate. Sure, Arkin was a maniac who tried to wipe out the human
		race for his own personal art project. But that was him. I always thought of myself as the type
		of person who would be incapable of taking the life of even the most despicable person.
	</p>
	<p class="mb-4">
		Arkin pulled his hand away and rubbed it. He would have only felt a tiny pinprick, and he
		probably thought that my ring merely scratched him. "Most people are like you," he said. His
		eyelids drooped a little. "They only see how things are and not how things should be."
	</p>
	<p class="mb-4">"I've seen a lot of things today," I said.</p>
	<p class="mb-4">
		"I know you don't believe me," Arkin said. He swayed like he had too much to drink. "But you
		will."
	</p>
	<p class="mb-4">"You feeling ok?" I said, pretending to be concerned.</p>
	<p class="mb-4">Arkin rubbed his temple. "No. I'm suddenly very tired."</p>
	<p class="mb-4">"Why don't you lie back?" I said.</p>
	<p class="mb-4">
		Arkin sat back and leaned against the side of the plane. He closed his eyes. I watched him until
		his breathing stopped. He was dead by the time we were in the air. The flight attendant assumed
		that Arkin was sleeping, and when we landed, I got off the plane before anyone figured out he
		was dead.
	</p>
	<p class="mb-4">
		Then I <i>walked</i> out of the terminal on my <i>legs</i>! Arkin was already out of my mind. I
		skipped the taxi this time around and opted for the train. I had a very safe and uneventful ride
		back to the city. I spent all afternoon in the park, walking every trail and going up and down
		every set of stairs I could find. When I finally made it home, I took a nice long bath, shaved
		my legs, and savored the feeling of the razor sliding over my skin. I drifted to sleep that
		night curled up in a ball so I could hug my shins.
	</p>
	<p class="mb-4">
		This morning I woke up, back in the present. The good news is my legs are still attached to my
		body. The bad news is I'm a bear. Like the animal. Brown, furry, and I guess about five hundred
		pounds. My bed broke under my weight.
	</p>
	<p class="mb-4">
		My best guess is that Arkin wasn't working alone. Whoever his partner or partners were, they
		continued his work and took it in a different direction. Like you, I've seen the news, and I
		know I'm not the only one who woke up as a bear today.
	</p>
	<p class="mb-4">
		If you ever had a random conversation with a stranger who talked about turning people into
		bears, please write about it on Facebook, Instagram, Twitter, and every other social network you
		can think of. Pray that there is enough of a functioning government left to spy on our
		conversations and that they still developed time travel in this timeline. I don't want to live
		the rest of my life like this.
	</p>
</span>
