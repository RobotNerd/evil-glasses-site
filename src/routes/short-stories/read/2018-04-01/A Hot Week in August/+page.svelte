	<h1 class="h2 mb-1 sm:h1 sm:mb-4">A Hot Week in August</h1>
	<h3 class="h4 mb-8 sm:h3">by Marshall Bowles</h3>

	<p class="mb-4">
		Mason dropped the shovel onto the dry, rocky ground. He leaned back against the truck beside
		Earl. "You don't want to travel through Europe?" Mason said. "Everybody wants to do that."
	</p>
	<p class="mb-4">
		"I ain't got no interest in traveling nowhere," Earl said. He pushed back his dirty baseball cap
		and swiped his hand across his forehead. He flicked the sweat off of his fingers. Some of it
		splashed onto Mason's boot.
	</p>
	<p class="mb-4">
		Mason frowned at the small wet spot on his boot. He used the heel of his other foot to try to
		scrape it off. "I'm just saying, you ought to get out and see the world. There's more to life
		than Greenville."
	</p>
	<p class="mb-4">
		Earl didn't look at Mason. Instead he stared straight ahead, out over the tall brown grass in
		the field. It hadn't rained in a month, and the land was suffering for it. One spark, and the
		whole state would go up. Everybody was worried about it, but there wasn't a whole lot they could
		do.
	</p>
	<p class="mb-4">
		"There's nothing out there I need to see. I'll leave the exploring to young folks like you,"
		Earl said.
	</p>
	<p class="mb-4">
		Mason shook his head. "You aren't that old yet. What are you, like thirty-seven?"
	</p>
	<p class="mb-4">
		"Thirty-<i>three</i>." Earl said. He turned halfway around and reached into the bed of the
		truck. He popped open the lid of the cooler and pulled out a soda. "Want one?"
	</p>
	<p class="mb-4">
		"Nah, I'm good right now," Mason said. Earl closed the cooler. He leaned back against the side
		of the truck and opened the can. Mason kicked in the dirt with the toe of his boot, making a
		small divot in the ground. A bit of dust puffed up into the air.
	</p>
	<p class="mb-4">
		The firebreak stretched off into the distance to their left. This was their fifth day on the
		job. Backbreaking work from sun up to sundown, using shovels and pickaxes to cut a wide dirt
		channel along the edge of the field. Old man Comstock didn't want to use a tractor out there—he
		said the noise would scare his prize-winning roosters. He was a crazy old coot, but Mason was
		glad to have the work, even if it was just part time. Jobs were hard to come by these days.
	</p>
	<p class="mb-4">"How do you know?" Mason said.</p>
	<p class="mb-4">
		"Huh?" Earl said, looking Mason in the eyes for the first time in the last hour.
	</p>
	<p class="mb-4">
		"How do you know there's nothing that would be interesting to you in Europe?" Mason said.
	</p>
	<p class="mb-4">Earl pursed his lips. "I don't want to talk about it."</p>
	<p class="mb-4">
		"You don't want to talk about it because you don't have a good reason. You're just making
		excuses," Mason said.
	</p>
	<p class="mb-4">
		"Whatever," Earl said. He reached in the truck and grabbed a shovel. He jammed the tip of the
		shovel in the dirt at his feet, then pulled on his leather work gloves. "Break's over."
	</p>
	<p class="mb-4">
		They spent the rest of the afternoon in silence, cutting a channel in the bone dry earth at a
		snail's pace.
	</p>
	<br />
	<p class="mb-4">
		A grasshopper hiding somewhere in the tall grass made its mating call. The high-pitched
		screeching sound echoed across the dry field. The air was heavy today, without even the
		slightest breeze to help alleviate the heat. Mason and Earl were only twenty yards further along
		from where they were yesterday. Mason figured it was good progress, all things considered.
	</p>
	<p class="mb-4">
		"I've been thinking," Mason said. The two men were sitting on the open tailgate, eating their
		lunches. Mason had a peanut butter and jelly sandwich on wheat bread. Earl was eating beans out
		of a can.
	</p>
	<p class="mb-4">
		"You do too much of that," Earl said around a mouthful of beans. "You're going to hurt your
		head."
	</p>
	<p class="mb-4">
		Mason ignored him. "I figure the reason you don't want to get out of Greenville is because
		you've never left this town. You don't know what you're missing because you don't know what
		you're missing."
	</p>
	<p class="mb-4">
		Earl dipped his spoon down in the beans and stirred them around. "That's about the dumbest thing
		I've ever heard."
	</p>
	<p class="mb-4">"Maybe," Mason said. He took another bite out of his sandwich.</p>
	<p class="mb-4">
		Earl scooped a spoonful of beans into his mouth. "Not everybody's got to think like you do.
		Different people see different things as being important."
	</p>
	<p class="mb-4">
		Mason finished his lunch. He tossed the empty sandwich bag in his lunch box and opened another
		bottle of water. Earl scraped the last of the beans into his mouth. He tossed the empty can over
		his shoulder into the truck bed.
	</p>
	<p class="mb-4">"What's important to you?" Mason said.</p>
	<p class="mb-4">
		Earl grabbed his shovel off the ground. "Right now, it's finishing another twenty yards before
		sundown."
	</p>
	<br />
	<p class="mb-4">
		By the late afternoon of the following day, they had reached the southern corner of the field,
		where it butted up against the forest. Towering columns of clouds had been forming in the
		distance for the past few hours, promising the relief of rain but carrying the threat of
		fire-starting lightning strikes.
	</p>
	<p class="mb-4">
		Mason stood up tall and stretched his back. A faint rumble of thunder echoed from a distant
		cloud. "Maybe we should call it a day."
	</p>
	<p class="mb-4">
		Earl kept cutting a groove in the rocky soil with his pickaxe. "It ain't that close. We can keep
		going."
	</p>
	<p class="mb-4">
		A drop of sweat ran down Mason's neck to his already soaked shirt. He was covered in grime that
		had mixed with the sweat to form a paste coating his skin. He took a long shower every night to
		scrub himself clean, but it felt like there was more grime every day.
	</p>
	<p class="mb-4">
		The edge of one of the clouds moved past the sun, providing welcome relief from the intense
		heat. "I'd rather not be out here when a storm comes—"
	</p>
	<p class="mb-4"><i>BOOM!</i></p>
	<p class="mb-4">
		The shock wave slammed into Mason so hard his bones vibrated. All he could see was white,
		brighter than the sun, searing his eyes. It was over in a split second, and he could see again.
		The sound of thunder reverberated off the distant hills, and his ears rang. Splinters of wood
		began falling out of the sky.
	</p>
	<p class="mb-4">
		Earl was on his back in the dirt. His eyes were wild. "What the hell was that?" he yelled.
	</p>
	<p class="mb-4">
		"Lightning!" Mason said. He pointed at a nearby tree. The trunk was split in two, almost down to
		the ground. Earl scrambled to his feet and ran to the truck. Mason crawled in the cab after him.
	</p>
	<p class="mb-4">
		Within seconds, the sky opened up and rain hammered down on the truck. It was like someone
		poured a bucket of water on them, the rain was so thick they couldn't see out of the front
		windshield. They sat there in silence, watching the rain, both trying to calm down after the
		close call with the lightning strike.
	</p>
	<p class="mb-4">
		After a while the rain lessened, though it didn't stop. Mason wondered if Comstock would tell
		them to stop work on the firebreak now. He hoped not. As much as he didn't like doing
		backbreaking work, he really needed the money.
	</p>
	<p class="mb-4">
		"My old man died when he was fifty-three," Earl said. He looked out at the field, the tall brown
		grass now bent over from the power of the storm. "He worked in the warehouse his whole miserable
		life, and then he just died. He never got to enjoy living."
	</p>
	<p class="mb-4">
		Earl leaned back and looked up at the roof of the cab, his head resting against the back
		windshield. "But at least he had a steady job, enough to support my mama and me. All those jobs
		are gone now. I'm as miserable as my dad ever was, except I don't have nothing. I'm never going
		to have money to buy a house and have a family.
	</p>
	<p class="mb-4">
		"I don't care about nothing outside of Greenville because I can't barely survive here," Earl
		said. "And you're in the same boat as me. Out here working a job that don't pay shit because
		there ain't nothing else."
	</p>
	<p class="mb-4">
		Earl turned and looked Mason in the eye. "What's important to
		<i>you</i>, Mason?"
	</p>
	<p class="mb-4">
		Mason started to speak but realized he didn't have an answer. Earl stared at him for a long
		time. Mason looked away. "That's what I thought," Earl said.
	</p>
	<p class="mb-4">
		The rain slowed to a drizzle. The men sat in the truck, not speaking, watching the rain. The
		entire length of the firebreak was a muddy trench. Mason wondered if it would still stop a
		wildfire. Would there even be a danger of that now? Probably not for a while.
	</p>
</span>
