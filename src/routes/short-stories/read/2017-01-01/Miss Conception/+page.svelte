	<h1 class="h2 mb-1 sm:h1 sm:mb-4">Miss Conception</h1>
	<h3 class="h4 mb-8 sm:h3">by Marshall Bowles</h3>

	<p class="mb-4">
		Henry pointed his laser pistol over his head and blindly fired a pair of warning shots out of
		the broken windows of his apartment. He yelled loud enough to make sure the cops on the street
		heard him. "Just turn around and leave!"
	</p>
	<p class="mb-4">
		The sound of a loudspeaker crackled, and a cop's distorted voice boomed into the room. "We want
		to resolve this peacefully, Henry. Nobody has to get hurt."
	</p>
	<p class="mb-4">
		"Ogre's blood! It's too late for that," Henry yelled. The thought of a standoff with the police
		had seemed exciting at first, but it was terrifying now that he was in the middle of it.
	</p>
	<p class="mb-4">
		"You know what kind of firepower we have," the cop said, sounding like he was talking through
		gritted teeth. "If we wanted to storm in there and take you out, you wouldn't be able to stop
		us."
	</p>
	<p class="mb-4">
		Henry laughed bitterly. "You and I both know you won't do it," he yelled. "You wouldn't want to
		damage the goods." Henry checked the power meter on his pistol. It was down to a quarter charge.
		He guessed it was enough to hold them off for another hour, at most. The whole situation felt
		like the time his guild, the Shatterkin, valiantly fought and lost to the orcs at Bloodridge
		Pass.
	</p>
	<p class="mb-4">
		"Keep thinking that," the cop said. "We have to bring you in alive, but your face doesn't have
		to be in one piece--" The bullhorn cut off mid-sentence, like someone yanked it out of the cop's
		hand.
	</p>
	<p class="mb-4">
		Henry dreaded this day ever since Congress passed the TRIBAL Act. After years of the mainstream
		media reporting on the dwindling numbers of Navajo, it was inevitable that the nanny state would
		pass a law requiring people with Navajo ancestry to procreate. Henry happened to be one hundred
		percent Navajo, and now he was facing a future of involuntary servitude as a father.
	</p>
	<p class="mb-4">
		"Give them hell, Dragonstar!" The voice of Steelhorn came through on his earbuds. Only Henry's
		brothers-in-arms knew him as Dragonstar, his character name in the online role-playing game
		Eldritch Curse. The physical world was a boring place of meaningless shit, and Henry's true
		place in life was inside the game. Steelhorn was Henry's Shatterkin guild mate, raiding partner,
		and the best friend Henry ever had.
	</p>
	<p class="mb-4">
		"Thanks, Steelhorn," Henry said. He glanced at the recently-purchased cameras stuck to the walls
		inside his apartment. He was live-streaming the confrontation. "How's it looking on social
		media?"
	</p>
	<p class="mb-4">"Yeah man, you know, we got people on your side. Lots," Steelhorn said.</p>
	<p class="mb-4">"Tell it to me straight, sword-brother," Henry said.</p>
	<p class="mb-4">
		"Fuck," Steelhorn said. "It's split thirty-to-seventy against you. The fucking sheeple want the
		goddamn feds to run everybody's life."
	</p>
	<p class="mb-4">
		Henry's heart sank. The plan was a failure. Henry had wanted to make a run for the Mexican
		border, but Steelhorn had convinced him that a standoff with the police was his best shot. It
		was supposed to inspire enough public support to overturn the TRIBAL Act.
	</p>
	<p class="mb-4">
		"Listen," Steelhorn said. "It doesn't matter. Fuck those people. You have to keep fighting for
		yourself. Blacknight blades stand strong!"
	</p>
	<p class="mb-4">
		"Blacknight blades stand strong!" Henry said. It was their battle cry. Henry tried hard to sound
		confident.
	</p>
	<p class="mb-4">
		"Henry, let's take a step back and try this from the beginning. Clean slate." It was the cop on
		the bullhorn again. His calm voice sounded forced, a weak dam holding back an angry flood of
		cursing. "What do you say?"
	</p>
	<p class="mb-4">
		"Fuck off, goblin spawn!" Henry yelled. His voice cracked. He was getting hoarse from all the
		yelling.
	</p>
	<p class="mb-4">
		The cop ignored the insult. "We've got seven ladies out here who would like to meet you. All of
		them are full-blooded Navajo, just like you. I'm going to let them tell you about themselves."
	</p>
	<p class="mb-4">
		The bullhorn crackled with static, and a woman spoke. "Hi Henry, my name is Debbie. I've lived
		in the Navajo Nation my whole life. I teach Biology at Din√© College, and I know I will make a
		wonderful mother."
	</p>
	<p class="mb-4">
		"You're fat and ugly," Henry yelled without bothering to look. Who knows, maybe she was fat and
		ugly. It did not matter to him. Besides, he was sure a police sniper was outside just waiting
		for an opportunity to take Henry down with a tranquilizer gun.
	</p>
	<p class="mb-4">
		The cop jumped back on the bullhorn. "Listen here asshole, you show these women some respect.
		They're doing their duty to save your people."
	</p>
	<p class="mb-4">
		"They aren't my people!" Henry yelled. His parents left the reservation before he was born, and
		Henry lived a typical white bread suburban fucking American-dream life. He knew nothing about
		his ancestry and he did not care. "My people are the Shatterkin of the Windy Wilds."
	</p>
	<p class="mb-4">
		The bullhorn cut off, but Henry could hear the cop's distant voice saying, "What the fuck does
		that mean?"
	</p>
	<p class="mb-4">"Steelhorn," Henry said. "What do I do, man?"</p>
	<p class="mb-4">"That depends on how far you're willing to go," Steelhorn said.</p>
	<p class="mb-4">Henry felt sick to his stomach. "I'm not going to kill anybody."</p>
	<p class="mb-4">
		"Fine, fine. I wouldn't recommend it anyway." Steelhorn said. "Dragonstar, you gotta think about
		what they want from you. They're going to try to make you have babies with all those women out
		there to preserve the race."
	</p>
	<p class="mb-4">
		"I don't want to raise any kids," Henry said. He would never have time to play Eldritch Curse if
		he had to take care of a bunch of his own little bastards.
	</p>
	<p class="mb-4">
		"I know you don't," Steelhorn said. "But listen, they want a biological father to raise the
		kids. See what I'm getting at?"
	</p>
	<p class="mb-4">"No," Henry said.</p>
	<p class="mb-4">
		"They won't have any need for you if you can't make babies," Steelhorn said. "So all you got to
		do is blow off your balls."
	</p>
	<p class="mb-4">
		"What?" Henry's voice went up two octaves. He felt a tingling sensation in his crotch.
	</p>
	<p class="mb-4">
		"No sperm, no kids, and you keep your freedom." Steelhorn said it so calmly, like he was
		discussing the benefits of dragon armor versus regular steel.
	</p>
	<p class="mb-4">"No fucking way," Henry said. "You're out of your elf-forsaken mind."</p>
	<p class="mb-4">"It's your only option," Steelhorn said.</p>
	<p class="mb-4">
		The cop came back on the bullhorn. "I'm getting tired of this. You can do this the fun way, or
		we can strap you down and have a doctor jam a big fat needle into your testicles. Your choice.
		Five minutes and we're coming in there to get you."
	</p>
	<p class="mb-4">
		"You can't argue them down," Steelhorn said. "They've made up their minds, and they aren't going
		to let you have a say. You have to blow off your balls."
	</p>
	<p class="mb-4">Henry's heart beat faster. "I can't..."</p>
	<p class="mb-4">
		"Shh," Steelhorn said. "Don't worry buddy, I'll talk you through it. Just put the gun barrel
		against your balls."
	</p>
	<p class="mb-4">
		Henry was shaking, but he did as Steelhorn instructed. He held the laser pistol with both hands
		to keep it steady. The thin fabric of his pants did nothing to soften the cold hard steel
		pressing against his sensitive scrotum.
	</p>
	<p class="mb-4">
		"Put your finger on the trigger," Steelhorn said. A bead of sweat rolled down the side of
		Henry's face.
	</p>
	<p class="mb-4">
		"On three, you're going to pull the trigger," Steelhorn said. Henry gritted his teeth.
		"One...two...THREE!"
	</p>
	<p class="mb-4">"Argh!" Henry pointed the gun at the wall. "I can't do it!"</p>
	<p class="mb-4">
		"Goddammit Dragonstar!" Steelhorn said. "Once you're in custody, you're going to have people
		watching over you twenty-four seven. You'll never get a moment of free time, and you'll never be
		able to play Eldritch Curse again. They'll turn you into livestock."
	</p>
	<p class="mb-4">"Three minutes," the cop said.</p>
	<p class="mb-4">Steelhorn spoke forcefully in his ear. "Blow. Off. Your balls."</p>
	<p class="mb-4">
		Henry jammed the gun against his crotch again, and his finger caressed the trigger. He
		swallowed.
	</p>
	<p class="mb-4">"Blacknight blades stand strong!" Steelhorn said.</p>
	<p class="mb-4">"Blacknight blades stand strong!" Henry said.</p>
	<p class="mb-4">
		The two of them chanted in unison. "Blacknight blades stand strong! Blacknight blades stand
		strong!"
	</p>
	<p class="mb-4">
		Henry screamed. He was going to do it. Fuck the cops. Fuck the system. His finger hovered over
		the trigger.
	</p>
	<p class="mb-4"></p>
	<p class="mb-4"></p>
	<p class="mb-4">
		Sike Dehaaya turned off the holotape. The simulation dissolved and the rocky surface of Mars
		reappeared around him. His young Navajo students blinked as their vision adjusted to the bright
		reddish haze of the sunny Martian afternoon. He took a deep breath of the terraformed air and
		sighed. Revisiting the past always left him in awe of the strange lives led by his distant
		ancestors.
	</p>
	<p class="mb-4">
		"And that is how the genetic purity of our tribe was preserved by the great man known as Henry
		Wilkins," Sike said. "Any questions?"
	</p>
