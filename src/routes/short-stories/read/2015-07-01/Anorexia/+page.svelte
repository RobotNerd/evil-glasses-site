	<h1 class="h2 mb-1 sm:h1 sm:mb-4">Anorexia</h1>
	<h3 class="h4 mb-8 sm:h3">by Marshall Bowles</h3>

	<p class="mb-4"><strong>8/5 Wed</strong></p>

	<p class="mb-4">First day back at school, and it's already shit.</p>

	<p class="mb-4">
		Ms. McReady--our cow of a biology teacher--forced us into assigned seats. She sat me down beside
		this skeleton of a girl. The girl had long stringy black hair, hunched shoulders, and her
		clothes were almost falling off. She smelled gross. I haven't smelled anything like it before.
		The best way I can describe it is stale.
	</p>

	<p class="mb-4">
		Then I realized who it was. Sandy. Holy shit! I must have looked like an idiot, sitting there
		with my mouth wide open. She had needed to lose weight, but damn she took it too far. She had to
		have starved herself the entire summer to get that small. I felt guilty. Maybe I shouldn't have
		called her a fat bitch.
	</p>

	<p class="mb-4">
		Whatever though. She needed to hear it. I looked at the front of the room and tried to ignore
		her. She just sat there staring straight ahead the whole time, acting like I didn't exist.
	</p>

	<p class="mb-4">
		Everything was fine until the bell rang. I wanted to get out of the room, but when I grabbed my
		bag, Sandy looked at me. Oh God, her eyes were sunken into her head. She wasn't really looking
		at me, more like through me at something in the distance. She looked so weak that I have no idea
		how she could even move around.
	</p>

	<p class="mb-4">
		Then her eyes focused on me, and she checked me out from head to toe. She said, &quot;Why are
		you so fat?&quot; I gasped. It felt like she dumped a bucket of ice water on me, and I was on
		fire at the same time. And my skin felt odd, like something was crawling all over me.
	</p>

	<p class="mb-4">Sandy tried to smile, but it was forced. A tear rolled down one of her cheeks.</p>

	<p class="mb-4">I ran away.</p>

	<p class="mb-4"><strong>8/6 Thu</strong></p>

	<p class="mb-4">Sandy didn't show up to school today.</p>

	<p class="mb-4">
		I told Amanda and Cici about what happened. Neither of them have seen Sandy yet, so they don't
		know how bad she looks. Amanda laughed about it. She said that it doesn't matter how much weight
		Sandy loses, because being skinny won't improve her face.
	</p>

	<p class="mb-4">
		I said that maybe I had been too hard on Sandy, but Cici told me Sandy was a weirdo. My choice
		to cut Sandy out was a necessity, because being friends with her was social suicide. I know Cici
		is right, but I still feel guilty. Sandy and I had been close ever since we were little.
	</p>

	<p class="mb-4"><strong>8/7 Fri</strong></p>

	<p class="mb-4">
		I was grabbing stuff from my locker, and I felt someone behind me. The stale smell was enough
		for me to know it was Sandy. I glanced back at her and felt sick. She looked worse than before.
	</p>

	<p class="mb-4">I took a deep breath, faced her, and asked what she wanted.</p>

	<p class="mb-4">
		Sandy clutched her notebook to her chest like a shield. I thought she was going to cry. Instead,
		all she said was, &quot;I'm sorry.&quot; I could barely hear her, she said it so quietly. She
		turned around and walked away.
	</p>

	<p class="mb-4">I'm such a fucking asshole.</p>

	<p class="mb-4"><strong>8/10 Mon</strong></p>

	<p class="mb-4">No show again on Sandy. I thought it was going to be a normal day. Stupid me.</p>

	<p class="mb-4">
		We were in the cafeteria for lunch. Pizza day. I didn't feel like talking, so I left the
		conversation to Amanda and Cici. Like usual.
	</p>

	<p class="mb-4">In the middle of a mouthful of pizza, I looked up and noticed a creepy guy.</p>

	<p class="mb-4">
		It was some gross old man. He must have been 60, at least. He stood all the way on the other
		side of the cafeteria, so it was hard to see him well. He looked bald and pale, and he was
		wearing a dirty one-piece outfit that was almost the same color as his skin. It was kind of like
		what a mechanic would wear.
	</p>

	<p class="mb-4">
		The creep was staring at me. Even from so far away, I could feel his eyes crawling all over me.
		I was so disgusted I had to spit out my food. Amanda and Cici were like WTF? I tried to point
		him out, but he had disappeared. Scary. The school should do background checks on their
		janitors.
	</p>

	<p class="mb-4">I lost my appetite after that.</p>

	<p class="mb-4"><strong>8/11 Tue</strong></p>

	<p class="mb-4">
		I've been sick all day. Mom said I had a fever. She tried to give me chicken noodle soup, but I
		felt nauseous at the sight of it. No school today.
	</p>

	<p class="mb-4">
		It's late, and I'm feeling a little better. I'm still a little queasy, but I think I can go back
		to school tomorrow. Mom thinks I had food poisoning.
	</p>

	<p class="mb-4"><strong>8/12 Wed</strong></p>

	<p class="mb-4">Sandy is dead.</p>

	<p class="mb-4">
		Mr. Willard called an emergency school assembly in the morning. He got up in front of everyone
		and told us that Sandy had passed away on Monday. They brought in counselors to talk to us.
	</p>

	<p class="mb-4">
		Marisa Wright and Lorry Masterson were sitting behind me. I could hear them whispering to each
		other. Marisa said that Sandy had committed suicide, and that the school was worried about other
		students killing themselves.
	</p>

	<p class="mb-4">
		Lorry said that she heard it wasn't suicide. Sandy was murdered. Some serial killer had gotten
		into her bedroom while her whole family was asleep. Sandy had been cut up into a bunch of pieces
		and all of her skin was taken.
	</p>

	<p class="mb-4">
		Marisa said that was a bullshit story someone made up to start an urban legend. They must have
		noticed me trying to eavesdrop. They shut up after that.
	</p>

	<p class="mb-4">I cut class for the rest of the day. I'm not talking to the counselors.</p>

	<p class="mb-4"><strong>8/13 Thu</strong></p>

	<p class="mb-4">I don't know what to do.</p>

	<p class="mb-4">
		Dad cooked steak for supper. I was starving. Between being sick and finding out about Sandy, I
		haven't been able to eat anything since Monday.
	</p>

	<p class="mb-4">
		We were all sitting at the dining room table, and I had just put a piece of steak in my mouth. A
		horrible feeling came over me, exactly like last week when Sandy called me fat. It was like the
		beginning of a fever where your whole body aches, and I felt sick like I had eaten spoiled food.
		There were invisible tentacles running all over my skin.
	</p>

	<p class="mb-4">
		I don't know why, but I looked over my shoulder and out through the bay windows. The janitor
		from school was standing on the other side of the street staring at me through the windows. I
		almost threw up.
	</p>

	<p class="mb-4">
		I screamed. Mom and Dad freaked. I tried to tell them about the man standing outside, but I was
		crying and I couldn't get the words out. I kept pointing at the windows and Dad went to look.
		The guy ran off before Dad got out there.
	</p>

	<p class="mb-4">
		Dad called the police, but they said there's nothing they can do. Dad is taking off work
		tomorrow to go by the school with me.
	</p>

	<p class="mb-4"><strong>8/14 Fri</strong></p>

	<p class="mb-4">They don't have a new janitor at school.</p>

	<p class="mb-4">
		Dad and I were sitting in Mr. Willard's office. I tried to describe the creep to him, but Mr.
		Willard kept saying that they hadn't hired anyone new. He and Dad kept eyeing each other. As if
		I can't read body language.
	</p>

	<p class="mb-4">
		I yelled at them for not believing me. Mr. Willard looked right at me, and in the most
		condescending tone of voice, he said, &quot;If someone like that was lurking around the school,
		don't you think someone else would have noticed?&quot;
	</p>

	<p class="mb-4">
		This is the worst day of my life. I haven't eaten anything and I'm starving, but every time I
		think about that monster I get sick to my stomach.
	</p>

	<p class="mb-4"><strong>8/16 Sun</strong></p>

	<p class="mb-4">
		I hid in my room all weekend. Mom and Dad tried to get me to come out, but I faked being sick.
		They didn't believe me. Mom tried to have a &quot;serious&quot; talk with me about Sandy, but I
		told her I wasn't ready to talk about it yet.
	</p>

	<p class="mb-4">
		Mom left food for me. I flushed it down the toilet when she left. I'm so hungry, but just the
		thought of food makes me want to puke.
	</p>

	<p class="mb-4"><strong>8/17 Mon</strong></p>

	<p class="mb-4">
		It happened in the sophomore hallway between second and third period. I was so hungry that I had
		to eat something no matter how sick I felt. I bought a bag of chips from the vending machine.
	</p>

	<p class="mb-4">
		As soon as I put the first chip in my mouth, that sick feeling started again, the worms crawling
		over my skin. For some reason I looked up...and there he was. The creepy guy. He was standing at
		the far end of the hall, staring at me. The hallway was packed, but everyone was walking around
		him. Nobody even noticed him. It's like he was invisible to everyone but me.
	</p>

	<p class="mb-4">
		I felt like vomiting, but I didn't. I was so hungry. I stared that fucker down and forced
		another chip in my mouth. When I swallowed, he smiled.
	</p>

	<p class="mb-4">He took one step towards me.</p>

	<p class="mb-4">
		My hands were shaking, but I wouldn't look away from him. I pulled another chip out of the bag
		and ate it. When I swallowed, he took another step forward. A long string of spit came out of
		his mouth and dripped onto the floor.
	</p>

	<p class="mb-4">I took my phone out of my bag. My hand was shaking when I took a photo.</p>

	<p class="mb-4">
		He wasn't in the image. He wasn't there at all! I looked over my phone to double check, and he
		was definitely there. I took another photo. Nothing. All the people around him showed up, but
		not him.
	</p>

	<p class="mb-4">
		I dropped the chips on the floor. I was shaking so bad that I had to use both hands to steady
		the phone. Picture. Picture. Picture. He was never visible in any of them.
	</p>

	<p class="mb-4">
		That's when I noticed a small crowd had stopped to watch me. I realized I was crying. I stuffed
		my phone back in my bag, and I pointed at the man. The creature. They looked that way, and then
		they looked right back at me. They looked confused. The creature smiled.
	</p>

	<p class="mb-4">I ran away.</p>

	<p class="mb-4"><strong>8/18 Tue</strong></p>

	<p class="mb-4">
		I'm losing too much weight. My clothes don't fit right. I'm so hungry I can't think straight.
	</p>

	<p class="mb-4">
		Today went by in a blur. I stayed away from food but couldn't stop thinking about it.
		Hamburgers, mac and cheese, fries, ice cream. Every time I imagine taking a bite, I see the
		monster's face smiling at me and taking one step closer. I'm so miserable, I just want to shoot
		myself in the head.
	</p>

	<p class="mb-4">This is what happened to Sandy. It has to be.</p>

	<p class="mb-4"><strong>8/19 Wed</strong></p>

	<p class="mb-4">
		Mom made me sit down at dinner even though I said I was feeling sick. I was so weak from hunger
		that I couldn't put up a fight. She gave me a bowl of chicken soup.
	</p>

	<p class="mb-4">
		I stared at it for a while. When I looked up, both Mom and Dad were staring at me. They were
		worried but trying to hide it. I made myself smile, and then I ate a spoonful of soup.
	</p>

	<p class="mb-4">
		I felt him right away. Those slimy worms crawling under my skin. The nausea. He was behind me
		again on the street. I didn't look. I asked Dad if there was anyone outside behind me.
	</p>

	<p class="mb-4">He looked out the window, then back at me, and he shook his head.</p>

	<p class="mb-4">
		I ate more soup. It was so hard to keep it down. My stomach was trying to vomit out everything I
		put in. It was such a horrible feeling, the sickness and this insane hunger mixed together.
	</p>

	<p class="mb-4">
		When I finished, I looked back over my shoulder. The janitor was standing at the edge of our
		lawn.
	</p>

	<p class="mb-4"><strong>8/20 Thu</strong></p>

	<p class="mb-4">
		Cici and Amanda cornered me in the girl's bathroom. I've been avoiding them. Cici asked me what
		my problem was, and I told them that I was sick. They called bullshit and said they knew I
		wasn't eating. Amanda said that I was making them look bad and that I better put myself
		together.
	</p>

	<p class="mb-4">
		I cried. I tried to tell them about the man--the monster--but I know I wasn't making sense. They
		just looked at me like I was a stray dog. Cici kept shaking her head in disappointment. I've
		never felt so ashamed.
	</p>

	<p class="mb-4">
		I finally gave up because I knew they wouldn't believe me. They helped me clean up my face, and
		we left. They left me alone for the rest of the day.
	</p>

	<p class="mb-4">I'm so hungry.</p>

	<p class="mb-4"><strong>8/21 Fri</strong></p>

	<p class="mb-4">
		I didn't talk at school today. Not a word to anyone. I didn't feel like it. Dad had to work late
		and Mom couldn't put up enough of a fight to force feed me.
	</p>

	<p class="mb-4">
		I took a long look at myself in the mirror. By body is fading away. I used to be so beautiful,
		but now my cheeks are hollow, dark bags hang under my eyes, and my hair is stringy.
	</p>

	<p class="mb-4">
		In a moment of weakness, I dug through the drawer looking for a razor blade. I pulled it out
		before remembering it was a cheap safety razor. Too hard to get the blade out.
	</p>

	<p class="mb-4">What the fuck is wrong with me? I don't want to die.</p>

	<p class="mb-4">Sandy. I need to find out what happened to Sandy.</p>

	<p class="mb-4"><strong>8/22 Sat</strong></p>

	<p class="mb-4">
		I used a knife from the kitchen to poke a new hole in my belt. My pants are falling off.
	</p>

	<p class="mb-4">
		While I was heading out the door, I grabbed a bagel. I tried to act cheerful, and I yelled back
		at Mom that I was going to the mall. Maybe she believed me. I threw the bagel in the trash.
	</p>

	<p class="mb-4">
		I drove to Sandy's neighborhood and parked a couple streets away from her house. Kids were out
		riding bikes and playing ball and stuff. It made me want to be little like that again.
	</p>

	<p class="mb-4">
		Sandy's house was empty. Police tape crisscrossed the front door. Her family must not have come
		back there since the murder. I wonder if they will ever come back.
	</p>

	<p class="mb-4">
		I found the special rock in the backyard, but I could barely lift it. My arms were shaking by
		the time I flipped it over. The emergency house key was still there, thank God. Sandy's parents
		told her to never, ever tell anyone outside of her family about it, but she told me. The alarm
		code to the house was the same.
	</p>

	<p class="mb-4">
		I had to stop to catch my breath halfway up the stairs to the second floor. My stomach growled.
		I thought about raiding their kitchen for food. For a moment, the face of the janitor was all I
		could see. My body dry heaved. When I got myself back under control, I climbed the rest of the
		way to Sandy's room.
	</p>

	<p class="mb-4">
		Lorry was right. Dried blood covered everything. The worst of it was a huge patch staining the
		carpet beside the bed, but there were splatters on the walls and even the ceiling.
	</p>

	<p class="mb-4">
		Small, bloody handprints made a trail across the floor and along the wall, ending in a large
		patch of blood. They were the size of Sandy's hands, so they had to be hers. There was one much
		larger handprint on the wall. It could have been a man's hand, except each fingertip was pointed
		like a claw. I looked away so I wouldn't be sick again.
	</p>

	<p class="mb-4">
		I tried to find Sandy's diary. We started writing them together when we were little, and I know
		she kept up with it too. It wasn't anywhere in her room. The police probably took it as
		evidence. Luckily, they didn't find her secret compartment. I'm the only person Sandy ever told
		about it. She taped a piece of cardboard under her dresser to make a hidden shelf.
	</p>

	<p class="mb-4">
		The first thing I pulled out was a picture of me. It was a 5x8 from photo day in middle school.
		Sandy had crossed out my eyes. She wrote a message on the bottom. &quot;I thought you were my
		friend.&quot;
	</p>

	<p class="mb-4">
		The only other thing in the compartment was a piece of lined notebook paper covered in sketches.
		A large image of a head covered the center of the page, with smaller versions scattered around
		it. All were drawings of the janitor.
	</p>

	<p class="mb-4"><strong>8/23 Sun</strong></p>

	<p class="mb-4">
		Dad carried me downstairs to the dining room table. I tried to fight back, but I'm so weak now
		that I couldn't do anything. They stood on the other side of the table and watched me. They
		wouldn't leave until I finished my whole plate of food.
	</p>

	<p class="mb-4">I was crying and trying to fight them, but I was so hungry too.</p>

	<p class="mb-4">
		As soon as the first piece of asparagus touched my lips, I could feel him behind me. I glanced
		back through the windows. He was standing in the middle of the front yard, smiling and drooling.
	</p>

	<p class="mb-4">
		I ate the rest of the plate. I didn't look back, even though I knew he took a step with every
		bite.
	</p>

	<p class="mb-4">
		Mom and Dad looked even worse by the time I finished. I was crying and shaking. They just don't
		know. They can't see him.
	</p>

	<p class="mb-4">
		Dad helped me out of the chair so he could take me back to my bedroom. I tried not to look, but
		I couldn't stop myself. The janitor was standing in our living room.
	</p>

	<p class="mb-4">He's going to get me. Oh god he's going to get me.</p>

	<p class="mb-4"><strong>8/24 Mon</strong></p>

	<p class="mb-4">
		Amanda and Cici sat with me in the courtyard during lunch. They think it will make them look
		compassionate to take pity on me. They were both gorging themselves on food--so much food! They
		talked with their mouths full, not even bothering to be courteous.
	</p>

	<p class="mb-4">
		Disgusting. It was like watching a bunch of pigs wallow in filth. Amanda turned to me, gave me a
		fake smile, and asked if I wanted a bite of her ham sandwich. I screamed at her. &quot;Why are
		you so fat?&quot;
	</p>

	<p class="mb-4">
		I don't know why I said that. It wasn't what I meant to say...it just came out. Immediately
		after I said it, I felt the grossness pour over me again, even though I hadn't eaten any food.
		Nausea. My skin felt like it was trying to crawl off of my body.
	</p>

	<p class="mb-4">
		Amanda shuddered. She stared wide-eyed at me for a few seconds before regaining her composure.
		Then she stood up and grabbed Cici's arm. The walked away from me.
	</p>

	<p class="mb-4">
		Out of the corner of my eye, I saw movement from across the courtyard. When I turned my head, I
		saw him. He was standing very far away, on the other side of the lawn by the gym doors. He
		wasn't looking at me. His head moved slowly, and I followed his gaze. He was watching Amanda.
	</p>

	<p class="mb-4">
		I'm so sorry, Amanda. I passed this curse to you just like Sandy gave it to me. I shouldn't have
		let it get this far.
	</p>

	<p class="mb-4"><strong>8/25 Tue</strong></p>

	<p class="mb-4">
		I couldn't sleep last night. School was a blur. I avoided Amanda and Cici, and I'm sure they
		don't want to talk to me anyway.
	</p>

	<p class="mb-4">
		Mom and Dad didn't try to make me eat again. After I went upstairs, I waited and listened to
		them. They were whispering, but they weren't being quiet enough. They're going to put me in the
		hospital and jab an IV in my arm.
	</p>

	<p class="mb-4">
		I can't take this hunger anymore. I have no other options. I've thought about slitting my wrists
		or even using Dad's pistol, but everyone would think it's a suicide. I don't want people to
		think I killed myself.
	</p>

	<p class="mb-4">
		After Mom and Dad went to bed, I snuck down to the kitchen and got a bowl of peanuts. I'm
		sitting on the bed now with them in front of me. My phone is tucked away on my bookshelf, and
		it's recording. The police will go through my things and find the video. They might not be able
		to see the monster, but they will see what he's going to do to me.
	</p>

	<p class="mb-4">I ate a peanut. I can feel him. He's just outside of the door.</p>

	<p class="mb-4">
		Oh fuck! I ate another peanut, and he was just suddenly in my room. He didn't float through the
		door or anything. He was outside one second, and when I swallowed he was just...there.
	</p>

	<p class="mb-4">
		He looks different now that he's close. His skin is thin. I can see the black veins underneath,
		and he's covered in swollen lumps like angry pimples. His skin moves on its own, almost like a
		sheet flapping in the wind.
	</p>

	<p class="mb-4">
		I always thought he was wearing a one-piece uniform. It's not. It's a suit made of skin. There
		are patches of hair in a few places, from different people. I can tell because the hairs are
		different colors. I guess once he kills me, he's going to use my skin for part of his outfit.
	</p>

	<p class="mb-4">
		His face is horrible. I can't tell what color his eyes are because they're clouded over. The
		skin under his eyes sags down onto his cheeks, and I can see bone under there. His teeth are
		huge and flat, like a horse.
	</p>

	<p class="mb-4">I'm shaking. I can barely write anymore.</p>

	<p class="mb-4">
		It's time. I just want to get it over with. I'm going to put this diary on the top shelf in the
		closet so my blood won't make it illegible.
	</p>

	<p class="mb-4">
		I love you Mom. I love you Dad. I just want you to know this wasn't your fault. There wasn't
		anything you could do to help. This is a monster. A real monster.
	</p>

	<p class="mb-4">Three more peanuts. Three more steps. It will finally be over.</p>
</span>
