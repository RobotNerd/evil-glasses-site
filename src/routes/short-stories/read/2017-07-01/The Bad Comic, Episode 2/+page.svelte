<span class="w-2/3 max-w-md">
	<h1 class="h2 mb-1 sm:h1 sm:mb-4">The Bad Comic, Episode 2</h1>
	<h3 class="h4 mb-8 sm:h3">by Sara Lufrano</h3>

	<p class="mb-4">
		Write a stupid joke. Be funny. Say something clever. At least make fun of someone for fuck's
		sake. No? Why the hell not? Just make fun of that girl's leggings. They are ugly. Say something
		like “I hope she didn't pay for those.” I know it isn't funny. Jesus Christ. But it's something.
		Get over yourself and say something.
	</p>
	<br />
	<p class="mb-4">
		I hadn't had a contract come through for weeks. I was doing busy work. Organizing files, looking
		at new document management software, standardizing clauses, or wasting the Wi-Fi by watching
		Netflix and YouTube.
	</p>
	<p class="mb-4">
		I hadn't been to a comedy club in months. After I got some laughs with the few okay jokes I
		wrote, I ran dry. I watched every comedy special I could but hated most of them.
	</p>
	<p class="mb-4">I think I'm over comedy. I don't think I like it anymore.</p>
	<br />
	<p class="mb-4">Colleen carried two clear drinks back to the table we were sitting at.</p>
	<p class="mb-4">“Happy hour is over in 30 minutes,” she said.</p>
	<p class="mb-4">“Shit,” I got up once she put her drinks down. “I'll get two more.”</p>
	<p class="mb-4">“This one is for you.” She was confused.</p>
	<p class="mb-4">
		“Ah,” I brushed away her words, “Just in case it gets slammed and we don't make it back to the
		bar before it's over.”
	</p>
	<p class="mb-4">
		It was hard hanging out with people. They wanted to talk about what they were doing and then ask
		how I was doing. I didn't want to tell them.
	</p>
	<p class="mb-4">
		Two drinks. Drink these two drinks and you can leave. Go home, sit in front of your stupid
		notebook and write nothing. Nice, productive night. Alone.
	</p>
	<p class="mb-4">I got my drinks and sat back with Colleen.</p>
	<p class="mb-4">“Have you heard from Rhett?” she asked.</p>
	<p class="mb-4">I hung my head. “God damn, Colleen. How about, how was my weekend?”</p>
	<p class="mb-4">“Oh, how was your weekend?” She took a sip from her drink and smiled at me.</p>
	<p class="mb-4">“Shitty and boring.”</p>
	<p class="mb-4">“What did you do?”</p>
	<p class="mb-4">“Nothing. What did you do?”</p>
	<p class="mb-4">
		“It's impossible to do nothing. When I was a kid my Uncle would call and when I would answer he
		would ask 'What are you doing?' I would say 'Nothing' and he would tell me, 'You can't do
		nothing.' So I always tell him, and anyone else, what I'm doing.”
	</p>
	<p class="mb-4">
		She looked at me waiting for me to tell her what I did over the weekend. “Jerked off and drank,”
		I said.
	</p>
	<p class="mb-4">“See, that's not nothing.”</p>
	<p class="mb-4">I smiled reluctantly. “What did you do this weekend?” I asked again.</p>
	<p class="mb-4">“I went out with that guy I had been messaging.”</p>
	<p class="mb-4">“Oh, yeah. Was he everything his charming messages suggested?”</p>
	<p class="mb-4">
		She pursed her lips and looked past me. “He was one of the biggest jerks I have met in this
		city.”
	</p>
	<p class="mb-4">Her eyes glassed over with tears. I figured as much, though.</p>
	<p class="mb-4">
		“Hey, hey.” I reached over and patted her on the shoulder. “There are millions of people here.
		There will be another.” I looked around. “Like that guy!”
	</p>
	<p class="mb-4">
		She looked at the chubby, balding guy with a neck beard that I was pointing to. She looked back
		to me with a blank look on her face.
	</p>
	<p class="mb-4">“You really aren't funny,” she told me.</p>
	<br />
	<p class="mb-4">
		Are jokes about not being funny, funny? Are jokes about not being able to make something funny,
		funny? Are jokes about trying to be funny but not being funny, funny?
	</p>
	<p class="mb-4">No and they never will be.</p>
	<p class="mb-4">
		Self-deprecation could be funny. I suck, I'm bad at this. I haven't written a joke in months. I
		barely even have a handful of good jokes. And that's my set, good night everyone. Thanks for
		coming. Tip your bartender. Who doesn't tip the bartender, anyway? I don't think I've ever seen
		anyone stiff a bartender.
	</p>
	<p class="mb-4">Why the fuck do people say that?</p>
	<p class="mb-4">
		That's a stupid joke. Fuck the guy that got away with saying that and having some stupid asshole
		chuckle.
	</p>
	<br />
	<p class="mb-4">
		I sat at my desk clicking around on Netflix when an email from Rhett came in. I didn't open it
		right away. I didn't want to seem like I had nothing better to do than read any email I got
		right away, no matter who it was from.
	</p>
	<p class="mb-4">
		I picked a British crime drama and went to the email. I was only cc'd on it. It was just an FYI
		about something that could happen, just in case it needed a contract. Waste of my damn time.
	</p>
	<p class="mb-4">“Steph!” Colleen leaned into my office.</p>
	<p class="mb-4">“Hey.”</p>
	<p class="mb-4">“I have a date tonight.”</p>
	<p class="mb-4">“See, there's a million people to date here.”</p>
	<p class="mb-4">“I know,” she smiled and twisted her torso back and forth. “You were right.”</p>
	<p class="mb-4">“Where are you guys going?”</p>
	<p class="mb-4">
		“Were getting drinks at Pier 99 and then dinner somewhere if it all goes well.”
	</p>
	<p class="mb-4">I sarcastically smiled for her. “Very cool.”</p>
	<p class="mb-4">
		“Thanks.” She stayed in my office looking at my whiteboard. There was only a list of music to
		listen to on it. “I'm going to keep asking you about him. You should just tell me what happened
		with Rhett.”
	</p>
	<p class="mb-4">
		“Bye, Colleen. Have fun on your date. I hope you two have beautiful kids.” I put my ear buds in
		and clicked my mouse loudly and banged on my keyboard.
	</p>
	<p class="mb-4">
		She smiled and waved as she left. I shook my head, rolled my eyes, and sighed. The trifecta.
	</p>
	<br />
	<p class="mb-4">
		The thing that upsets me when people ask me about Rhett is that I know for certain he's not
		anything like what I made him out to be. He wasn't even the type of person everyone else thought
		he was.
	</p>
	<p class="mb-4">
		We worked it out that he would come visit for a weekend. We went out, had fun, came back to my
		place, had sex, drank coffee in the morning, and then he left early.
	</p>
	<p class="mb-4">
		I messaged him too many times and got no response to most of them until he stopped all together.
		The last message he sent me was, “Don't tell anyone about us. It was a one time thing.”
	</p>
	<br />
	<p class="mb-4">
		It feels like I'm mad at him, furious that that's all he ended up being, but I know I'm mad at
		myself. I didn't need to make him what he wasn't, the perfect man and the only person on the
		planet that could make me happy.
	</p>
	<p class="mb-4">
		And when I think about being funny I think about that night and how hopeful and happy I was and
		I don't want to be funny anymore.
	</p>
	<p class="mb-4">That's what happened with Rhett.</p>
	<br />
	<p class="mb-4">
		Colleen and I were out after work. She was talking about the guy she was dating now, how great
		he was and how she hoped he's the one.
	</p>
	<p class="mb-4">“How can you hope someone is the one?” I asked.</p>
	<p class="mb-4">“I just want him to be the one, then.”</p>
	<p class="mb-4">“He either will be or he won't be. You can't make people be the one.”</p>
	<p class="mb-4">“But we can grow into it.”</p>
	<p class="mb-4">“Isn't that something you both are supposed to feel right off the bat?”</p>
	<p class="mb-4">She smiled, “Maybe we did and we just aren't telling each other yet.”</p>
	<p class="mb-4">“You better tell him soon.” I finished my drink and looked for the waiter.</p>
	<p class="mb-4">“I'm not worried about it, for once.”</p>
	<p class="mb-4">Her optimism was tiring.</p>
	<p class="mb-4">“Let's talk about something different,” I said.</p>
	<p class="mb-4">“Okay. Did you hear the diss track Tanya la Tigre put out yesterday?”</p>
	<p class="mb-4">
		I smiled hoping this would be a good distraction. “No, sounds like a fake name, and tell me
		everything about it.”
	</p>
	<br />
	<p class="mb-4">I know I'll get over Rhett. But I don't know if I'll ever be funny.</p>
</span>
